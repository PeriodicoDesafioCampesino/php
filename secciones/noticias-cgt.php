			Con el fin de presentarle las propuestas campesinas al ministro de agricultura  Andres Valencia , el día 26 de septiembre se dieron cita los presidentes de FANAL , ACC Y NLC, la base del dialogo se basó en la carta que se le envió al presidente Duque el día 1 de agosto de 2018.<br><br>


			<strong> REFORMA SOCIAL AGRARIA INTEGRAL </strong><br><br>

			1. Entrega a los campesinos, a través de sus organizaciones, de los predios incautados al narcotráfico, específicamente a nuestras organizaciones campesinas, solicitamos la entrega de 1.400.000 hectáreas en función de lo social.<br><br>

2. Las organizaciones campesinas, representadas por la Acción Campesina Colombiana, ACC, la Federación Agraria Nacional, FANAL y Nuevo Liderazgo Campesino, NLC, la Federación Nacional de Artesanos de Colombia FENARTEC, las Comunidades Indígenas y Negras y Comunidades del Sector Agroindustrial filiales de nuestra Confederación, organizaciones que fueron creadas a la luz de la constitución, que poco han sido tenidas en cuenta los diferentes gobiernos; desde los inicios de las negociaciones con los grupos armados hemos manifestado que no se sentían representados en ellos, por esta razón insistimos en las propuestas que permitan el fortalecimiento de las Organizaciones Campesinas, Comunidades Indígenas y Artesanos de nuestro país <br><br>

3. Con base en los derechos de las campesinas y los campesinos,  principalmente el derecho a la vida y a un nivel de vida digno, el derecho a la tierra y el territorio, el derecho a las semillas y al saber y las prácticas agrícolas tradicionales, el derecho a los medios de producción agrícola, el derecho a la información y a la tecnología agrícola, la libertad para determinar el precio y el mercado para la producción agrícola, el derecho a la protección de valores en la agricultura, el derecho a la diversidad biológica, el derecho a preservar el medio ambiente, la libertad de asociación, opinión y expresión y el derecho de acceso a la justicia, nos permitimos presentar al Gobierno algunas solicitudes para buscar la reivindicación de nuestros derechos. <br><br>

4. Aclaramos que, las siguientes propuestas se podrán materializar con la debida voluntad política para presentar por iniciativa gubernamental una Política Pública Nacional y un Proyecto de Ley donde se recojan las Propuestas del Sector Campesino, Indígena y Artesanal, recopiladas en la propuesta de proyecto de ley denominado “Ley de Tierras y Desarrollo Rural Alternativo”, garantizando así el funcionamiento del Sistema Nacional de Desarrollo Rural y Reforma Agraria con participación de las Organizaciones Campesinas, Comunidades Negras e Indígenas.<br><br>

<strong>Solicitamos al Gobierno en Materia de Tierras </strong><br><br>

5. Revisar los términos de reconocimiento de la propiedad, a partir de 1950 y el de las comunidades negras desde lo ancestral e histórico, inclusive para que se aplique a comunidades indígenas con fundamento en la ley 70 de 1993 y la ley 89 de 1890.<br><br>

6. Iniciar la discusión y concertación de los límites de la propiedad para evitar que el derecho de superficie no sea requisito para la restitución de tierras, permitiendo a los propietarios definir su vocación y uso. El derecho de superficie puede constituirse en un mecanismo para evitar que las personas en situación de desplazamiento y despojada, vuelvan a sus tierras. <br><br>


		
			
