		<font size="4">
		<p> 
El Instituto Nacional de Estudios Sociales, INES de Colombia, está celebrando sus cincuenta y seis años de formación integral de los trabajadores organizados en Colombia. Es una institución de formación sindical y social, creada en 1964 para la calificación, cualificación y organización de cuadros en la Confederación General del Trabajo, CGT. En sus programas han participado decenas de miles de afiliados de organizaciones sindicales y sociales; cooperativas y campesinas; mujeres y jóvenes trabajadores, del sector público y privado, formal e informal, de la ciudad y del campo.</br></br>

Es de destacar la Visión prospectiva y de futuro que hoy más que nunca cobra importancia y vigencia sinigual: El INES de Colombia, se ha propuesto elevar el proceso y el sistema de formación de la CGT a los mejores niveles de excelencia y calidad, acordes y exigidos por las nuevas situaciones, los cambios, los desafíos y las necesidades actuales y futuras del Movimiento de los Trabajadores en todo su accionar.</br></br>

Su protagonismo ha sido marcado por la fidelidad a su Misión: Brindar formación y capacitación a los trabajadores en los distintos sectores poblacionales por medio de seminarios, talleres, foros, diplomados, coloquios, programas especializados, equipos de estudio-reflexión-acción y planes de autoformación, para rescatar en el Movimiento de los Trabajadores el compromiso de clase, la transformación profunda y radical de las mentalidades y estructuras de la sociedad actual, mediante el desarrollo de una conciencia moral revolucionaria.         </br></br>

Todo su quehacer formativo está orientado a formar a los afiliados de la CGT para que tengan capacidad de:</br></br>
•	Promover el proceso de formación de la conciencia de los trabajadores (formación ideo-política, o educación para la vida),</br>
•	Impulsar el desarrollo organizacional y las posiciones del Movimiento (formación político-estratégica),</br>
•	Administrar los planes, programas y actividades de la CGT en todos los niveles de su estructura (formación temática y táctica, o capacitación para el trabajo),</br>
•	Fomentar la sinergia interna que permita cohesionar las actividades de las diferentes áreas del desarrollo organizativo (formación de cuadros para la acción),</br> 
•	Animar en la dimensión latinoamericana y caribeña el pensamiento humanista y emancipador liderada por la Alternativa Democrática Sindical de las Américas ADS, fortaleciendo los procesos de un sindicalismo de valores y principios (formación ética), </br>
•	Promover el diálogo social y la participación ciudadana con el fin de  interactuar  en los  diferentes escenarios  nacionales (formación ciudadana).</br></br>

La experiencia concreta en formación sindical y socio laboral que ha adelantado el INES es muy importante por su trayectoria y el sendero que ha marcado como ejemplo para otras organizaciones, además por la regularidad y continuidad. La formación que se imparte en el INES es tanto formación para el desarrollo personal y de las organizaciones de trabajadores, como capacitación para el trabajo y educación para la vida. </br></br>

El INES ha tenido una amplia oferta de temas de formación para todos los sectores laborales y sus asuntos de interés, y han realizado publicaciones, estudios académicos e investigaciones sobre la realidad social, económica, política y laboral, para formular propuestas de políticas de seguridad social, de incrementos salariales, de generación de empleo y formas de contratación, así como de un nuevo modelo de desarrollo, basado en la economía solidaria y el cooperativismo. </br></br>

Conscientes siempre de la necesidad y el compromiso de trascender el valor instrumental y las cortapisas ideológicas de la educación, y entusiasmados con la  misión de formar para la emancipación de la conciencia, para el discernimiento y para la acción, los contenidos y la metodología del Instituto se han orientado a la libertad del hombre, que, dueño de su propio destino, vive fraternalmente entre iguales en sociedades democráticas; que no solamente se domina a sí mismo, sino también a la naturaleza; además, que se reviste de poder y vitalidad en sus relaciones y en el logro de su liberación social, y que, como sujeto-agente de su realidad, emplea sus facultades para transformarla e integrarse con su comunidad en la constitución  de una nueva sociedad.
Convencidos de que la emancipación de la clase trabajadora será obra de los mismos trabajadores, el INES ha desarrollado un proceso de formación del pensamiento dialéctico, interrogativo e innovador, centrado en el sujeto, un trabajador adulto, activo,  práctico, reflexivo, necesitado y exigente, que interactúa y  construye procesos sociales de enseñanza-aprendizaje por medio de una metodología activa-participativa-creativa que genere una visión autogestionaria y procure que se cumpla el dicho andragógico según el cual el organismo en desarrollo aprende mientras crece y crece mientras aprende.</br></br>

Ahora podemos decir con orgullo que el INES de Colombia es la primera y la única escuela de formación de los trabajadores organizados con que cuenta la sociedad colombiana, creada en el seno de la clase trabajadora y puesta exclusivamente a su servicio.</br></br>


