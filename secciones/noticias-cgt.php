	•	Carta Encíclica LAUDATO SI del Santo Padre Francisco “Sobre el cuidado de la casa común”: por medio de esta encíclica se plantea un redireccionamiento ético del consumo, la producción y las formas de relacionarnos con el medio ambiente a la vez que abre las puertas al diálogo entre religiones basándose en esfuerzos llevados a cabo por otras iglesias diferentes a la Católica. </br></br>
	
