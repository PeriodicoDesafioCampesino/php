	  	Finalmente, para muchos líderes y dirigentes del sector de la economía solidaria, así como para Jairo Orlando Villabona, profesor de la Facultad de Ciencias Económicas de la Universidad Nacional de Colombia (UNAL), en su documento publicado el 09 de julio de 2020, tomando en cuenta el coeficiente de Gini, el Banco Mundial ubica a Colombia como el segundo país más inequitativo de América Latina, después de Honduras. Dicho indicador se mide en un rango de 0 a 1, donde cero significa que todos tienen el mismo ingreso, y 1 que una persona concentra todos los ingresos. Cuanto más elevado es el coeficiente, mayor es la desigualdad. En el país el Gini está en 0.53. Una manera de revertir este factor es hacer más colectiva la propiedad: no es posible que en 2017 solo cuatro grupos económicos nacionales y otros multinacionales tuvieran el 71 % de las operaciones económicas de Colombia. </br></br>

		Infortunadamente la legislación cooperativa colombiana no ayuda mucho a pensar en este tipo de transiciones, por cuanto las reservas legales que se hacen constituir de los excedentes son de un 50%, mientras que en Argentina y Chile no pasan del 15% y 18 % respectivamente; además, en la mayoría de los países del mundo los excedentes cooperativos se pueden distribuir a los socios, y en Colombia no.</br></br>

		De hecho, si se disminuye el monto de reservas del 50 % que se exige en Colombia, incluso tomando países de la región queda un monto de entre 85 y 82 % para tomar la opción de capitalizar las cooperativas o distribuir excedentes como se hace en casi todo el mundo, lo que haría que muchas personas pensaran en invertir en las cooperativas como una muy buena opción de propiedad colectiva y de rentabilidad; también se debe eliminar la norma que obliga a las cooperativas a distribuir las reservas patrimoniales a terceros y no a sus asociados.</br></br>

		Tales factores frenan el crecimiento de este tipo de instituciones y desincentivan la inversión en las organizaciones cooperativas. Por eso, el Gobierno y la Superintendencia de la Economía Solidaria deben replantear sus políticas obsoletas para ponerse a tono con la legislación cooperativa internacional. </br></br>

		En un contexto de crecientes desafíos económicos, sociales y medioambientales que ponen en riesgo la subsistencia de los medios de vida, al menos tal como los conocemos, la economía social y solidaria se constituye una opción para orientar la organización de la economía y la sociedad, pero es menester conocer sus particularidades dado que en cada historia de fracaso de estas organizaciones se encuentra el uso indebido de la figura legal, acciones de supervisión y de promoción públicas inadecuadas, la asimilación con empresas capitalistas o el desconocimiento de sus asociados en cuanto a las particularidades económicas y el necesario equilibrio entre la orientación económica y la incidencia social. </br></br>

		Por otra parte, temas como la inflación, el empleo, la pobreza y la dotación servicios básicos, que centraban las preocupaciones de los economistas hasta hace sólo un par de décadas, han sido parcialmente solventados (ONU, 2015) aun cuando estos mismos temas están adquiriendo nuevos matices por los cambios impulsados por la revolución tecnológica. </br></br>

		El conocimiento y la tecnología han avanzado de manera exponencial, pero parecen insuficientes para resolver las crecientes desigualdades sociales. Mientras se descubren exoplanetas y se discute su habitabilidad, los conflictos geopolíticos se acentúan, algunas democracias retroceden hacia modelos hegemónicos configurando cambios de actores y una creciente despreocupación por preservar los derechos de los que piensan diferente. </br></br>

	La mercantilización de toda actividad humana como la salud, la educación, el medioambiente, la cultura y el deporte están a la orden del día y hacen parte de las bases de un insostenible modelo de crecimiento económico caracterizado, entre otras, por:</br></br>

	•	La desconexión entre la economía y las necesidades de las personas lo que ocasiona un aumento sostenido en el precio de bienes esenciales, meritorios y fundamentales (como la vivienda, el transporte y los servicios públicos),</br>


	•	El incumplimiento de la promesa de tasas de crecimiento de la producción, el comercio y el desacople de la acumulación económica basada en la economía real dadas las desproporcionadas tasas de reproducción de dinero usado para comprar más dinero (CEPAL, 2016),</br>


	•	Y la vinculación de la economía actual a la reproducción creciente de la desigualdad y la devastación ambiental (Oxfam, 2017) Sin duda es un contexto que luce a toda vista adverso para la sobrevivencia planetaria.</br></br>



	En el centro del modelo actual subyace la separación intencionada del accionar integral de los individuos lo que conduce a la despreocupación colectiva por el bien común. La promesa de que la racionalidad centrada en el individualismo puede agregarse para lograr óptimos económicos, éticos, sociales, ambientales, culturales, espirituales y políticos entre otros, es una deuda que no podrá ser pagada dado que el individualismo no puede por sí solo generar una evolución sostenible, ni de la humanidad ni de los sistemas sociales. De esto hay una amplia evidencia empírica que muestra desde la biología, la antropología, la neurociencia, la pedagogía, la psicología y las ciencias económicas, el rol preponderante que tiene la acción común, el altruismo y la cooperación en la Explicación de la sostenibilidad de la especie humana y entorno (Singer y Ricard, 2015).</br></br>

	Sin embargo, hay una movida creciente que nada contracorriente. Se trata de movimientos conformados por ciudadanos preocupados por rescatar los quehaceres y sabidurías de sus ancestros, volcarse hacia esquemas orgánicos de consumo y de producción sostenible, de generar sistemas económicos incluyentes generadores de bien común. Estos movimientos hacen parte activa de la sociedad civil de un territorio, pero tienen la capacidad de actuar en los mercados, de relacionarse con los agentes del Estado y de desarrollar iniciativas que responden a preocupaciones medioambientales. En este marco la economía social y solidaria tiene la virtud de servir de puente entre la esfera pública y el mercado, entre la esfera ambiental y la social, democratizando y potenciando a la sociedad civil. </br></br>

	El siguiente documento de la Universidad Javeriana, Economía social y solidaria en el territorio: significantes y co-construcción de políticas públicas, liderado por el doctor Juan Fernando Álvarez Rodríguez, se manifiesta lo siguiente:</br></br>

