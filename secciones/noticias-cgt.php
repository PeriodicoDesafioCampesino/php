		 <strong>Por: Alvaro Parra Riveros </br> Secretario de finanzas ACC </strong>
		


		<font size="4">
		<p> </br></br></br></br>
			Se define a la Economía Solidaria como todas aquellas actividades sociales y económicas que contribuyen a que la economía coadyuve al desarrollo de la persona humana a través  de la solidaridad, la ayuda mutua, la cooperación y el trabajo en equipo, con una visión transversal que incluye iniciativas socio-empresariales en todos los sectores de la actividad económica. La Economía solidaria es por tanto una alternativa de desarrollo social y económico, que motiva una nueva forma de organización de la sociedad en torno a los recursos productivos y a su concepto de desarrollo y bienestar, en el marco de la Ley 454 de 1998 define a la Economía Solidaria en su artículo segundo, como el sistema socioeconómico, cultural y ambiental conformado por el conjunto de fuerzas sociales organizadas en formas asociativas identificadas por prácticas autogestionarias solidarias, democráticas y humanistas, sin ánimo de lucro para el desarrollo integral del ser humano como sujeto, actor y fin de la economía.</br></br>

			Para Jairo Orlando Villabona, profesor de la Facultad de Ciencias Económicas de la Universidad Nacional de Colombia (UNAL) en su publicación del 09 de julio de 2020:  “Según la Organización Internacional de las Cooperativas en la Industria y los Servicios (Cicopa, por sus siglas en inglés) indican que en Estados Unidos existen 29.285 cooperativas que generan cerca de 1,5 millones de empleos; en Argentina en las mutuales se registran 13.047 con cerca de 390.000 personas empleadas, y en Brasil 6.580 con 1,9 millones de empleados, razón por la que lideran el cooperativismo en América.</br></br>

		Colombia no se queda atrás ya que la  economía solidaria alcanzó en 2018 los 19,1 billones de pesos de ingresos –el 2,2 % del PIB–, en la que algunas de sus expresiones como Colanta aportaron 2,9 billones y Copidrogas 1,6 billones de pesos, lo cual evidencian su capacidad de intervenir de manera significativa en importantes actividades y sectores económicos.</br></br>

		Otras expresiobnes como las cooperativas dedicadas a la actividad financiera tenían cerca del 70 % de los asociados, el 51 % de ellos en cooperativas financieras de ahorro y crédito y el 15 % en los fondos de empleados. Tales datos evidencian una gran oportunidad para que el gremio del sector solidario fortalezca su crecimiento mediante acuerdos más contundentes. De hecho, aunque las actividades financieras son relevantes en la economía solidaria, son poco significativas en el consolidado nacional: los bancos cooperativos solo representaron el 0,8 % de todos los activos bancarios.</br></br>

		También se puede replicar en otras actividades que hoy no cuentan con empresas de economía solidaria, como por ejemplo en la industria –un sector fundamental en la generación de empleo–, o en sectores en los cuales todavía puede ganar más protagonismo, como el agro o la construcción.”</br></br>


		“Según la revista Semana del 28 de enero de 2020, este periodo “arrancó, en firme la nueva década. En Davos se reunió la elite mundial para discutir la necesidad de un capitalismo más social, la ONU y la ONG Oxfam publicaron sendos informes donde revelan una desigualdad histórica en el mundo... La élite pensando, la política convulsionando y la calle protestando. </br></br>

		La implementación de las diversas formas que hacen parte de la economía solidaria ha contado históricamente con grandes obstáculos. Uno de ellos es la dicotomía entre país rural vs país urbano. Un territorio rural marginado, donde no hay institucionalidad -o está capturada por mafias o clanes- donde asesinan a los líderes sociales ante la impotencia del Estado y que ha protagonizado el conflicto la guerra contra las guerrillas y los grupos ilegales y donde no ha existido el Estado por décadas es, y seguirá siendo, un pesado lastre que nos impide abrazar enteramente el siglo XXI. El 60 por ciento de nuestra desigualdad proviene de la brecha urbano-rural, según Rimisp. Es la tensión entre esas dos Colombias donde radica gran parte de nuestro problema estructural,” otro obstáculo es la normatividad existente para el sector solidario, que aunque cuenta con diversas normas no son autónomas y mucho menos independientes ya que están ajustadas no a su esencia de ser sino al modelo neoliberal rampante, caso específico las cooperativas de Ahorro y Crédito y las Financieras que se deben regir por las normas de la Superfinanciera, crudamente hay que decir que en este sentido el sector solidario repite la historia y sigue trabajando para el sector meramente capitalista, caso MEGABANCO conformado por Bancoop, Cupocrédito y Coopsibaté,  que el gobierno tuvo a bien entregarlo al grupo AVAL de sarmiento Angulo, específicamente al banco de Bogotá.  </br></br>

		Hay dos sectores que pueden contribuir mediante formas asociativas solidarias a cerrar las brechas sociales incluyendo a los territorios históricamente abandonados y victimizados al desarrollo económico social y político, con un crecimiento sostenible autosostenible, sustentable y autónomo, ellos son el ecoturismo y la producción agrícola. Estos dos pilares pueden aportar a la construcción de un modelo de desarrollo socio-económico, con políticas de largo plazo y sostenibles, que permita reducir nuestra dependencia económica del petróleo y la minería.</br></br>

		Por lo anterior, las organizaciones de economía solidaria, son una alternativa de solución a las diferentes necesidades de las personas que integran las distintas comunidades, para lo cual es necesario un acuerdo de voluntades tanto de  personas como entre las orgaizaciones, basado en compromisos ético-sociales entre los sujetos participantes, para que los objetivos propuestos se hagan realidad y se apliquen  los ejes  fundamentales  del  Consejo  Nacional  de  Economía Solidaria -CONES- (el cual hay que recuperarlo), de tal manera que se justifique la realización de proyectos específicos para la consolidación del sector la economía solidaria de acuerdo con la necesidad nacional y el mandato constitucional, mediante:</br></br>
	-	El financiamiento de proyectos productivos</br>
	-	El desarrollo de una pedagogía crítica para la educación solidaria</br>
	-	La promoción del emprendimiento empresarial solidario</br>
	-	La utilización pedagógica y didáctica de las tecnologías de información y comunicación en múltiples formatos de aprendizaje</br>
	-	La actualización normativa</br>
	-	La integración de esfuerzos, recursos y talentos institucionales</br></br>

	Por otra parte, es necesario que las acciones de las organizaciones de la economía solidaria se incorporen dentro de los planes de desarrollo de los gobiernos, no sólo con mandatos normativos sino con el resultado de la sistematización de prácticas y experiencias exitosas que sirvan de prototipo y de mediadoras pedagógicas para los organismos e instituciones solidarias que se comprometan el sector de la economía solidaria.</br></br>

	De igual manera, la implementación dela economía solidaria y el cumplimiento de sus objetivos, exige e implica que su quehacer comunitario continúe su impacto en lo regional y compromiso ético-social con las poblaciones vulnerables y con los estratos populares, asuma la responsabilidad de liderar los diferentes procesos y proyectos necesarios para:</br></br>

	- Diseñar y desarrollar mallas curriculares fundamentales para incorporar la solidaridad como tema transversal en todas las áreas de formación y cursos académicos de los diferentes niveles del sistema educativo nacional.</br>
	-Sensibilizar, motivar y acompañar la formación solidaria en los líderes comunitarios y en los dirigentes de las organizaciones solidarias existentes y de nuevos actores educativos para la solidaridad y la paz.</br>
	-Elaborar prototipos de materiales en múltiples formatos de aprendizaje para los diferentes medios de información y comunicación, con énfasis en las mediaciones pedagógicas adecuadas a los diferentes contextos socio-culturales. </br>
	-Promover y acompañar la realización de las actividades investigativas pertinentes para el desarrollo del sistema, de tal manera que participen las comunidades en la identificación de sus problemas y en la construcción colectiva de soluciones, para consolidar el sector solidario.</br>
	-Evaluar los procesos y los resultados de los diferentes proyectos específicos realizados en coordinación con los organismos e instituciones solidarias, comprometidas en la consolidación del sector solidario.</br>
	-Sistematizar y socializar las experiencias exitosas desarrolladas con la participación activa de los sujetos sociales, comprometidos en la construcción de una patria solidaria y en paz.<br></br>

	La realización de las funciones anteriores requiere repensar las instituciones responsables de la formación para la solidaridad y de la promoción de redes productivas de economía solidaria, a partir de los diferentes procesos de la gestión del conocimiento, para la transformación de la realidad local, regional, nacional e internacional, en el marco de un modelo de desarrollo alternativo. <br></br>


