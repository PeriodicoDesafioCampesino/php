	  	7. Decidir de manera concertada con las comunidades, organizaciones campesinas y las organizaciones étnicas, la implementación de las áreas de desarrollo rural, las zonas de reserva campesina y la posesión material de los territorios colectivos de las comunidades negras y los resguardos indígenas, de acuerdo a las realidades regionales y los planes de ordenamiento territorial, donde se garanticen la seguridad de soberanía alimentaria, reconociendo así la cultura campesina y de las comunidades negras e indígenas, las semillas nativas y los saberes populares con apoyo decidido y presencia permanente del Estado.<br><br>

8. Entregar a las comunidades campesinas de manera concertada con sus organizaciones,  los predios que están y serán administrados por la dirección de estupefacientes, transferidos al INCODER, deben ser entregados de manera preferencial y sin condiciones de convocatorias donde participen otros. <br><br>

9. Garantizar que el desarrollo empresarial rural con la agroindustria debe incluir a la comunidad rural de la región, no solo en el suministro de la mano de obra sino en la distribución de las utilidades de la empresa y el cumplimiento de la responsabilidad social empresarial.<br><br>

10. Garantizar el desarrollo empresarial, con el apoyo a la asociatividad rural a través de cooperativas, empresas comunitarias y demás formas colectivas para la producción, comercialización y distribución de los productos nativos.<br><br>

11. Limitar la venta de tierras a extranjeros, deteniendo así, la política de expansión del derecho de dominio a no nacionales. Identificar de manera clara la titulación de tierras que tienen los extranjeros, buscando con esto aclarar las adjudicaciones que se han hecho de manera ilegal, y de esta forma anular estas operaciones para que estas tierras formen parte del    fondo para la adjudicación de tierras para los que no las tienen.<br><br>

12. Permitir la compra directa de tierras por parte del INCODER para ser tituladas de manera colectiva a las comunidades afrodescendientes e indígenas, y campesinas<br><br>

13. Constituir el Fondo Nacional de Tierras, con diez millones quinientas mil hectáreas para ser adjudicadas con el propósito de desarrollar actividades de economía campesina, reconociendo de esta forma que en Colombia existen un millón quinientos mil familias campesinas sin tierra. <br><br>

14. Recuperar las tierras baldías que han sido indebidamente ocupadas y adjudicadas, con el fin de adjudicarlas a campesinos de escasos recursos, como lo señala la ley y la jurisprudencia constitucional.   <br><br>

15. No considerar los “Proyectos de Interés Nacional y Estratégicos” PINE, denominados así por el Documento Conpes 3762, como Proyectos de Utilidad Pública, ya que con esto lo que se desea es priorizar las solicitudes de titulación de baldíos a favor de entidades de derecho público desconociendo el derecho prioritario que tienen los campesinos a la propiedad rural. <br><br>

16. Asegurar la no intermediación en el proceso de restitución de tierras a través del fortalecimiento institucional.<br><br>

17. Contemplar, en la concertación con las víctimas la existencia de garantías necesarias para que la restitución sea material, esto es que no se agote con la entrega de los títulos sino que incluya garantías para el retorno y la permanencia en los territorios. <br><br>

18. Acelerar y asegurar la legalización de la devolución de tierras a las víctimas. <br><br>

19. Revisar por parte del Ministerio del Interior y Justicia en coordinación con el Ministerio de Agricultura, los lineamientos y la oferta existente en materia de protección y seguimiento de situaciones de riesgo, con enfoque diferencial de género, étnico y cultural, para quienes defienden y reclaman el derecho a la restitución de la tierra, la vivienda y el patrimonio en casos de despojo,  y en general para quienes reclaman el respeto, protección y garantía del derecho a la tierra y al territorio.<br><br>

20. Destinar del presupuesto nacional una partida para invertir en vías terciarias<br><br>

21. Propiciar y ratificar el convenio 141 de la OIT sobre las organizaciones de trabajadores rurales, para que este convenio y recomendaciones se conviertan el Ley de la Republica.<br><br>

