		<strong>Por: Andres Alejandro Cardona Castaño (octubre 2019) </br></br> ¿Qué es Señor Coffee?</strong></br>
		Señor coffee es una banda de rock Mestizo que se origina en el
municipio de Aguadas Caldas en el año 2009, Señor Coffee es
frescura y energía en medio de las montañas verdes de Colombia,
cobijadas por las sedosas brumas y el rocío inspirador de las
mañanas. Es ese aroma y sabor a café que reconfortan el día, es
mezcla de colores, cantos, letras y sonidos que evocan la memoria,
contrastados con la potencia y la vitalidad del Rock de nuestra
gente, el Rock mestizo de nuestro pueblo latino.</br></br>

Señor Coffee, ha desarrollado música propia de manera
independiente, brindando una perspectiva alternativa, sobre la
música colombiana, las representaciones y símbolos territoriales,
además una propuesta de cultura, para el rescate de los sonidos
tradicionales colombianos, la cultura de paz, la pedagogía cultural
desde la historia y la Geografía de la música, como medio de
expresión y emprendimiento.</br></br>
Señor Coffee ha participado en eventos de carácter local, regional
y proyección internacional, entre los más significativos están:</br></br>
-4° CIRCUITO INTERNACIONAL DE DANZA en Pareja</br>
-Concierto Pacto por las Artesanos Aguadeños (30 Diciembre 2016
Aguadas Caldas)</br>
-Festival BORONDO BIOCULTURAl (Pradera – valle 2016)</br>
-1° Concurso de Música Campesina/ Pasaje Cultural Cafetero,
Teatro Fundadores (24 junio Manizales 2017)</br>
-Festival Internacional EJE ROCK (20 de Agosto, Pereira
Colombia)</br>
-Festival: “LA FETE DE LA MUSIQUE”, Alianza Francesa, (17 de
Junio Manizales 2017)</br>
-Festival TAIQUENAJU, (Caicedonia Valle 28 de Julio de 2017)</br>
-Festival Internacional Grita Rock (Manizales 14 de Octubre de
2017)</br>
-28° Festival Nacional del pasillo colombiano (Aguadas 17 de
Agosto 2019)</br></br>
Cuentan con su primera producción musical llamada Zafarrancho
donde expresan toda su diversidad cultural: Colombiología
Montañera.</br></br>
<strong>Señor coffee en Facebook:</strong> “Somos Señor Coffee la
