	  <font size="4">
	  <p> 
	  Como amante del rock y de la buena música a mis 21 años siento que
Señor Coffee representa una manera distinta de ver la música
colombiana de origen campesino, al ser una banda de rock mestizo
nos muestra su música de manera agradable tanto para nosotros los
jóvenes como para las personas mayores, un gran ejemplo de esto es
su versión del tema el grillo, una canción típica para bailar en las
épocas de navidad, esta versión conserva la esencia original pero al
añadirle un toque moderno hace que todas las generaciones se
animen a bailarla.</br></br>

El grupo Señor Coffee es una representación de lo que puede ser la
música colombiana hecha por las nuevas generaciones, es una
renovación generacional que conserva la esencia de la música
colombiana pero llevándola a un contexto más moderno donde
podemos disfrutarla todos, su primera producción musical recibe el
nombre de Zafarrancho, en esta excelente producción musical está
incluida la canción con el mismo nombre que personalmente es la
canción que más me gusta dentro de este, esta canción tiene un
sonido muy agradable que dan ganas de bailar al escucharla y no solo
esto, la canción zafarrancho tiene una letra que hace una crítica a lo
que es el conflicto armado en Colombia.</br></br>

Señor Coffee es una banda que puede poner a bailar pasillo hasta los
más metaleros, situación que se vio en el festival internacional eje
rock en la ciudad de Pereira y en el festival grita rock en la ciudad de
Manizales en el año 2017.</br></br>

Su vestimenta siempre caracterizada por el uso del sombrero
aguadeño demuestra su amor por lo de la tierra y su manera de
mostrar el sombrero aguadeño como un símbolo de las cosas buenas
y quitarle ese estigma que le han dado algunas personas.  </br></br>

