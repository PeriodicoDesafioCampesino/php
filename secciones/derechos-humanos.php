      <div class="col s8" id="cuadro2">
      	<font size="4">
	  <p> 
	  Los niños son especialmente vulnerables durante los conflictos armados. Si bien el derecho los protege, son reclutados por las fuerzas armadas y otros grupos armados. Con frecuencia se ven separados de sus familias o alejados de sus hogares, y son víctimas de mutilaciones, abuso sexual y distintas formas de explotación.<br><br>
En situaciones de guerra, los niños gozan de la protección general del derecho internacional humanitario, al igual que la población civil y los combatientes. Pero también existen disposiciones especiales que reconocen su vulnerabilidad y necesidades particulares durante los conflictos armados.<br><br>
Aún hoy, los niños son reclutados por las fuerzas armadas de algunos países. En las últimas décadas, el reclutamiento de niños por grupos armados, en especial en África, se ha convertido en un problema humanitario grave. En muchos casos, los niños portan armas y participan activamente en los enfrentamientos. En otros casos, desempeñan funciones de apoyo, como el transporte de provisiones o la realización de tareas de inteligencia militar, lo que los coloca en situaciones de grave peligro.<br><br>
Los Protocolos adicionales de 1977 a los Convenios de Ginebra fueron los primeros tratados internacionales en los que se abordan estas cuestiones. Prohíben el reclutamiento y la participación de niños menores de 15 años en conflictos armados. Para los conflictos armados internacionales, el Protocolo adicional I establece que, cuando se requiera la incorporación a las fuerzas armadas de personas de entre 15 y 18 años, se otorgue prioridad a los de más edad.<br><br>
En la Convención sobre los Derechos del Niño de 1989, ratificada por casi todos los Estados, también se establece la edad de 15 años como el límite mínimo para el reclutamiento. Un protocolo facultativo de esta Convención, de mayo de 2000, eleva el límite de edad para el reclutamiento obligatorio a los 18 años y exhorta a los Estados a que eleven la edad mínima para el reclutamiento voluntario a más de 15 años. Insiste en que los grupos armados no deben reclutar menores de 18 años en ninguna circunstancia e insta a los Estados a penalizar ese tipo de prácticas.<br><br>
Aún no se ha logrado que la comunidad internacional reconozca los 18 años como edad mínima para la participación en conflictos armados, pero el Comité Internacional de la Cruz Roja (CICR) y sus socios del Movimiento Internacional de la Cruz Roja y de la Media Luna Roja están intentando lograr que se establezca ese límite de edad en el derecho internacional y en el de los distintos países.
En el terreno, el CICR presta ayuda en la desmovilización de niños soldados y les brinda apoyo psicológico, entre otras formas de asistencia necesarias para la reinserción de los niños en la vida social normal.<br><br>

</p>
