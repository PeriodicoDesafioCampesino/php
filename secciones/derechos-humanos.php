	  <p> 
	  Ante la pandemia del COVID-19, el Estado colombiano debe tomar
urgentemente las medidas adecuadas para garantizar los derechos
de los pueblos indígenas, entre ellos sus derechos a la salud, al agua
y a la alimentación, dijo Amnistía Internacional hoy. Estas medidas
deben coordinarse con cada pueblo indígenas, en respeto a su
derecho a la autonomía. </br></br>

En el marco de la emergencia sanitaria declarada por causa del
COVID-19 y del Estado de Emergencia Económica, Social y
Ecológica en todo el territorio nacional, el presidente Iván Duque
informó que las autoridades entregarían comida y dinero a las
personas más vulnerables. El Ministerio de Interior está encargado
de entregar alimentos a las comunidades indígenas, negras, raizales,
palenqueras, afrocolombianas, rom, Juntas de Acción Comunal,
líderes sociales y personas defensoras de derechos humanos.</br></br>

Sin embargo, después de tres semanas que la cuarentena fuera
impuesta, las comunidades indígenas de los departamentos de
Casanare, Vichada y Meta notificaron a Amnistía Internacional que
no han recibido apoyo de las autoridades gubernamentales, a pesar
de haber cumplido estrictamente con las medidas de aislamiento.
“Los pueblos indígenas en Colombia están en alerta máxima. El
gobierno está implementando medidas preventivas para el COVID-19
en el país, sin garantizar adecuadamente sus derechos
fundamentales. Si históricamente no han tenido acceso a la salud, al
agua o a la alimentación, en el contexto de esta pandemia esta
situación es muchísimo más grave porque no cuentan con
condiciones sanitarias y sociales para enfrentar adecuadamente el
COVID-19”, dijo Fernanda Doz Costa, directora adjunta para las
Américas de Amnistía Internacional.</br></br>

“Las autoridades colombianas deben adoptar medidas de protección
social adicionales para aquellas poblaciones que se encuentran en
situación especialmente vulnerable y están más propensas a
contraer COVID-19. Si las autoridades no toman acción urgente, los
pueblos indígenas se encontrarán en una encrucijada con dos
caminos impensables: o morirse de hambre o morirse por la
pandemia.” </br></br>

Hasta la fecha, la Organización Nacional Indígena de Colombia
(ONIC) ha reportado cuatro casos confirmados de COVID-19 en el
Pueblo Binacional Yukpa y el Pueblo Pasto y un posible caso en el
Pueblo Eperara Siapidara. El acceso a centros de salud desde la
mayoría de los territorios de estos pueblos indígenas es muy costoso
por lo lejano e inaccesible.</br></br>

<strong> “Si las autoridades no toman acción urgente, los
pueblos indígenas se encontrarán en una encrucijada
