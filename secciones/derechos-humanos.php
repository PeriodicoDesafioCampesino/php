	   <h5 class="header col s12 light"><strong>Continúan los asesinatos de líderes sociales en Colombia durante la pandemia      </strong></h5>
		 </div>
		 <h5 class="header col s12 center"><img src="../img/agosto2020/lideres.jpeg" alt="Esto es una foto" id="imgprincipal" ></h5></br>
		
