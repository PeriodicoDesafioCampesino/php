La directora para las Américas de Amnistía Internacional, Érika Guevara, habló con la Agencia Anadolu sobra la tendencia generalizada de asesinar a los líderes sociales y defensores de derechos humanos en varios países de Latinoamérica. Naciones donde sin importar la ideología de sus gobiernos se evidencia un fracaso en la protección de quienes velan por la seguridad del medio ambiente y de las comunidades rurales.<br><br>

<strong>A los líderes sociales y de derechos humanos los matan en países con gobiernos de izquierda y de derecha. ¿Hay un patrón que determine la persecución contra estas personas en el continente? </strong><br><br>

Cada país tiene ciertas características diferenciales en sus contextos de violencia, pero sin duda alguna hay tendencias muy comunes en toda la región. <br><br>

En general, las personas defensoras de la tierra y de los recursos naturales son quienes han estado más expuestas a la violencia radical y extrema en distintas naciones del continente, precisamente porque se enfrentan a los intereses económicos y políticos de una minoría que ha sido protegida por los estados, independientemente de los tintes ideológicos. <br><br>

