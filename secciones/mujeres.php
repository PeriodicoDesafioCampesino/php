La violencia contra las mujeres y las niñas es una de las violaciones de los derechos humanos más extendidas, persistentes y devastadoras del mundo, Una de cada tres mujeres sufre violencia a lo largo de su vida, independientemente de su estatus social, clase, raza, país o grupo de edad.</br></br>

En Colombia hay una cultura arraigada a la violencia contra la mujer, una vergüenza aceptada por muchos, y tímidamente contrarrestada por otros, las leyes existen pero no se aplican adecuadamente, sumado a ello la precariedad económica de muchas mujeres, que dependen económicamente de sus maltratadores, la dificultad para laborar, el tener la responsabilidad de los hijos y una sociedad   machista hace que la violencia contra la mujer sea una constante.
Un informe de la Fundación Feminicidios Colombia menciona que en hasta el mes  de Julio se presentaron  148 feminicidios,  58 casos se presentaron en zona rural y 90 en urbana.  Y  que el lugar más peligroso para las mujeres sigue siendo su propia casa, ya que el 56% de los casos presentados este año ocurrieron en sus viviendas., señaló que la mitad de las víctimas eran madres y que sus muertes dejaron 186 huérfanos, 56 menores de edad.</br></br>

Frente a esta realidad que nos sobrepasa el estado y la sociedad deben tomar medidas en la prevención que es En realidad la mejor manera de contrarrestar la violencia de género,  tratando sus orígenes y causas estructurales y  La prevención debe comenzar en las primeras etapas de la vida, mediante la educación de los niños y niñas que promueva las relaciones de respeto y la igualdad de género.</br></br>

Creando normas que promuevan  la igualdad de género  e implementen rutas de atención a las víctimas, más claras, rápidas y eficientes donde no se genere la alta tasa de impunidad que hace que las mujeres víctimas ya no quieran o teman denunciar.</br></br>

