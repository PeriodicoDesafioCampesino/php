	  <p> El 18 de diciembre de 2007, la Asamblea General de la
Organización de Naciones Unidas estableció el día 15 de octubre
como el Día Internacional de las Mujeres Rurales.
Es un día de reconocimiento por el aporte en el desarrollo de la
agricultura, como cultivadoras, productoras de alimentos que
contribuyen a la erradicación de la pobreza y la mejora en la
seguridad alimentaria.</br></br>

En Colombia la mujer rural ha hecho un aporte significativo en la
lucha en defensa de la paz, de los derechos humanos y del medio
ambiente, Reconocer su rol como constructoras de paz, como
individuos generadores de riqueza que hombro a hombro han
sostenido la agricultura sin abandonar las tareas de hogar,
reproducción y cuidado de sus hijos.</br></br>

Aun así la mujer rural lucha por la disminución de las brechas
entre el hombre y la mujer con la convicción de que haciendo del
campo un espacio equitativo entre los dos géneros, aportara al
crecimiento del desarrollo económico y social.</br></br>

Avanzar en la adopción de políticas públicas para el beneficio de
las mujeres rurales, es uno de los objetivos que han tenido las
mujeres rurales en cuanto al reconocimiento como sujetos de
derecho, pero la pobre aplicación de las políticas siguen siendo un
obstáculo que mantiene a la mujer rural en total vulnerabilidad, por
lo tanto la celebración de este día es más un llamado a los entes
del estado a tomar acciones a favor de las mujeres rurales para la
garantía de sus derechos, como garantizar el acceso a la tierras
priorizando los subsidios integrales, garantizar el acceso a la
restitución de la propiedad de las mujeres.</br></br>

La salud subsidiada de calidad y la protección social que le
permitan a la mujer rural acceder a una pensión vitalicia real.</br></br>

La educación, dado que el analfabetismo es un componente
importante en la exclusión social que afrontan las mujeres rurales.</br></br>

La construcción y mejoramiento de vivienda social.</br></br>

Crear estrategias de prevención y atención a violencias basadas
en género que es uno de los tantos y difíciles obstáculos con los
que las mujeres rurales tienen que enfrentar.</br></br>

En el día internacional de la mujer rural debemos visibilizar la
problemática de la mujer rural y reconocer el aporte vital que
hacen al bienestar de la población y al desarrollo del país. </br></br>
</p></font>
