Familias Católicas <br>
Mujer...Feliz de ser mujer<br>
La mujer y la familia<br>
Hoy más que nunca, el mundo necesita de la mujer.<br><br>

Por: Redacción | Fuente: Red Familia<br><br>

Cuando escuchamos la palabra hogar, nos imaginarnos un
espacio físico, que generalmente es la casa donde vivimos. Sin
embargo, el hogar no se refiere solo a un espacio físico, sino al
ámbito afectivo y espiritual, donde el ser humano es acogido,
aceptado y amado por todos los integrantes de su familia, y es
precisamente dentro de la familia, donde la mujer, con su
enorme capacidad de amar y entrega a los demás, es quien, en
la mayoría de los casos, logra convertir la casa en un hogar.<br><br>

Hay quienes dicen que la realización de la mujer solo puede
darse en el ámbito laboral incorporándose a un trabajo
remunerado, pero la realización de un ser humano, ya sea
hombre o mujer, no viene dado por trabajar dentro o fuera de la
empresa formal. Sin embargo, la mujer de nuestro tiempo vive
bombardeada por presiones sociales que la orientan, y casi la
obligan, a buscar su “realización” fuera de su hogar y su familia;
de lo contrario es criticada e incluso discriminada por ser ama
de casa y más aún cuando decide ser madre y cuidar a sus
hijos.<br><br>

