	  <font size="4">
	  <p> Uno de los desafíos centrales para la producción campesina es lograr que la juventud rural pueda quedarse en el campo y tenga las condiciones y conocimientos para producir y vivir dignamente, como se reconoció en el VII Congreso de la Coordinadora Latinoamericana de Organizaciones del Campo / CLOC-La Vía Campesina realizado en Cuba a fines de junio.</br></br>

“Las políticas públicas hacia la juventud de los gobiernos neoliberales de nuestros países no ofrecen las garantías que la juventud necesita en los territorios. Entonces, al no obtener una buena educación, formación, sistema de salud, el joven tiene que buscar en las ciudades esos beneficios con los que no contamos en el campo”, explicó en conversación con ALAI el dirigente hondureño, Raúl Eguigure, quien representa a la juventud centroamericana en la CLOC.  Por su parte, la argentina Margarita Gómez, representante por Suramérica y delegada al Comité Coordinador Internacional de la Vía Campesina por la juventud de las Américas, añadió que otro problema es el acceso a la tierra –sobre todo para las mujeres– y al empleo, por lo que se produce la migración forzosa a las ciudades en búsqueda de trabajo.</br></br>
No obstante, también se reconoce una barrera cultural: que al trabajo en el campo no se le da el valor suficiente, que es otro motivo de migración de la juventud. “Pienso que como campesinos deberíamos tener un título –afirma Eguigure– porque es una profesión. Nosotros producimos vida en el campo y las grandes ciudades no lo valoran. Si el campesinado dejara de producir, las grandes ciudades tendrían un caos alimentario”.  Por lo mismo, propone la concientización de la gente de las ciudades respecto al valor de lo que se produce en el campo, lo que motivaría también a la juventud a quedarse.</br></br>

Justamente, uno de los planteamientos centrales de la articulación juvenil es volver a enamorar a la juventud que se ha ido a la ciudad, respecto al campo. “Muchos de los jóvenes que encontramos en las grandes ciudades (sobre todo en las villas), son jóvenes cuyas abuelas, abuelos o tatarabuelos se fueron del campo por la condición difícil en la que vivían”, señala Gómez. “Hoy nuestra tarea como jóvenes es hacerle ver a la juventud que hay otro campo, otra realidad en nuestros territorios que hace setenta años atrás; y eso se debe a la lucha de nuestros ancestros, y a la que estamos dando hoy en día también en estos últimos veinte años”.</br></br>

Por ello, desde hace algunos años, se viene impulsando articulaciones entre jóvenes del campo y de la ciudad.  Mediante pasantías, campamentos, encuentros, los jóvenes de la ciudad tienen la oportunidad de conocer el campo, cómo se produce, principios de la agroecología; también se les invita a formarse en las escuelas de las organizaciones del campo.  Además, se promueve la agricultura urbana: “En varios países tenemos prácticas en que los jóvenes que no sabían cómo se sembraba una mata de maíz, ya lo hacen porque han ido al campo y han hecho la práctica –señala el líder hondureño–.  Y así, ellos en sus lugares, también tienen huertos familiares. Si tenemos un patio, una alberca, desde ahí podemos empezar a cultivar, y agroecológicamente también. Es una manera de obtener un beneficio viviendo en la ciudad. Nuestro sueño es que todos los jóvenes que han emigrado regresen al campo, porque en el campo está la vida”, añade Eguigure.  También impulsan procesos de organización en la ciudad.
</br></br>

A su vez, está articulación campo-ciudad permite que la juventud rural pueda conocer la ciudad y sus problemáticas y luchas y sumarse a sus movilizaciones.
</br></br>
