	  <p> 
	  	El pasado 9 de Septiembre se llevó a cabo reunión entre la
representación de la CRJT (Corporación regional de jóvenes
trabajadores) y el candidato a la Gobernación, Doctor Luis
Carlos Velázquez, donde se trataron temas sensibles de la
problemática actual del departamento para la juventud, allí se
mencionaron temas de gran preocupación como es: El acceso
a la educación superior, el derecho al primer empleo,
oportunidades laborales y con dignidad, problemáticas del
suicidio en los jóvenes así como de prostitución y
drogadicción.</br></br>

Se presentó un programa elaborado como propuesta
alternativa y de adopción con el fin realizar acuerdo
programático en aras de fomentar una política pública
fortalecida para ser más eficientes en la inversión de recursos
que hoy en día deben atender a más población joven.
Una política pública que cuente con principios incluyentes, con
objetivos claros y realizables, y además, tiene unos ejes
estratégicos que llevan a la praxis, a trascender el papel y a
tejer procesos que propenden por el empoderamiento de los
jóvenes como sujetos políticos, agentes de su comunidad,
vereda, barrio, institución educativa y en general
transformadores de su contexto.</br></br>

La consolidación y ejecución de proyectos para centros de
innovación, proyectos productivos dirigidos a poblaciones
focalizadas de acuerdo a las potencialidades, vocaciones
culturales y de producción especialmente agropecuarias pero
también de emprendimientos urbanos, actividades y
programas direccionadas en el fortalecimiento cultural, el
deporte, la valoración de sus raíces y las oportunidades de
desarrollo dentro de sus comunidades como atractivo propio
de su municipio y localidad, evitando así la migración a la
ciudad en busca empleos en su mayoría de veces resultando
de forma inmediata indignos, informales y de corto plazo con
vulneraciones salariales y laborales dado su oferta/demanda.</br></br>

En cuanto a los jóvenes desocupados los resultados saltan a
la vista, según cifras del DANE, a pesar de que la juventud en
Colombia es el 31,7 % de la población en edad de trabajar,
representa el 50 % de los desocupados del país, una situación
por completo ineficiente viéndose a nivel nacional, al no
aprovecharse esa fuerza para impulsar el aparato productivo
en general, fenómeno que se debe tratar en el departamento,
teniéndose en cuenta también con especial cuidado aquellos
jóvenes que cumplen el papel de proveedores de sus familias.
Se espera poder llevar a cabo todas estas acciones en pro de
las Juventudes del departamento Caldense y se sostiene la
voluntad y buen trabajo desde la CRJT en sus zonas de
acción, para mantener en constante búsqueda de los objetivos
propuestos como organización social.
