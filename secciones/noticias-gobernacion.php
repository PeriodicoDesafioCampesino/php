		<font size="4">
		<p>  
		Como exitosa calificaron los productores y organizadores la primera jornada de comercialización de la oferta productiva agropecuaria y agroindustrial de la región realizada por la Secretaría de Agricultura y Desarrollo Rural de Caldas, como parte de la política pública de Origen Caldas. A través de esta iniciativa, que busca dinamizar la economía de las familias campesinas de la región, se logró la venta de 254 remesas campesinas de productores del departamento.</br></br>

La secretaria de Agricultura y Desarrollo Rural, Ana Milena Nieto Ossa, agradeció a todas las personas de Manizales y Villamaría que con sus compras durante la primera edición de la Revista de Origen Caldas contribuyeron a la reactivación económica de los emprendedores del campo del departamento. “Nos sentimos muy satisfechos con esta primera versión, donde logramos vender 254 remesas de nuestras familias campesinas, remesas que sumaron un valor cercano a los 21 millones de pesos”, dijo.</br></br>

La funcionaria agregó que este fue el resultado de un trabajo que se realizó durante varios días, con el apoyo de todo un equipo logístico, encargado de recoger durante la semana todos los productos en las diferentes fincas. Posteriormente, y durante el fin de semana, este mismo equipo se encargó de empacar las remesas y de entregar todos los pedidos a los diferentes consumidores. Se garantizaron las condiciones de verificación y los protocolos de bioseguridad para que llegaran a cada uno de los hogares libres de cualquier riesgo de contaminación.</br></br>

Con relación a esta iniciativa, Johan Alexander Castañeda Serna, productor del departamento y quien también se vio beneficiado con esta estrategia, le agradeció a la Gobernación de Caldas por ese compromiso de apoyar al agro de la región. “Afortunadamente Caldas tiene todo el potencial en la producción de alimentos, diversidad con la que podemos reforzar mucho estas remesas e irnos ampliando. Si en este primer ejercicio estamos contando con más de 200 pedidos, imagínense en la medida en que nos vamos dando a conocer”, expresó.</br></br>

Finalmente, Nieto Ossa invitó a los caldenses a que sigan apoyando a los emprendedores del campo y a que hagan los pedidos en la segunda versión de esta iniciativa, la cual estará disponible en 15 días con una nueva oferta de productos en las páginas web y redes sociales de la Gobernación de Caldas y Origen Caldas.</br></br>

Los datos</br>
Origen Caldas es una política pública del Gobierno de Caldas a través de la Secretaría de Agricultura y Desarrollo Rural del departamento.</br></br>
El promedio de cada remesa adquirida por los consumidores fue de $82.000.</br></br>
Esta actividad de comercialización se realizará de manera permanente cada 15 días, con diferentes productores del departamento.</br></br>
Las ediciones de la revista de Origen Caldas podrán ser consultadas en las páginas web de Origen Caldas y la Gobernación de Caldas.</br></br>

