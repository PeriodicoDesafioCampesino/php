		<p>  
		La Gobernación de Caldas participó del 8 Encuentro Mujer y Café que se desarrolla en el Recinto del Pensamiento de Manizales. Allí, las mujeres caficultoras resaltaron el trabajo que ,a través del convenio ‘Mujeres cafeteras sembrando sostenibilidad’, la Secretaría de Integración y Desarrollo Social hace en busca de mejores condiciones económicas y sociales para esta población.<br><br>

