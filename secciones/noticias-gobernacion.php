	   <h5 class="header col s12 light"><strong>254 REMESAS CAMPESINAS POR UN VALOR CERCANO A LOS 21 MILLONES DE PESOS SE COMERCIALIZARON EN LA PRIMERA EDICIÓN DE LA REVISTA ORIGEN CALDAS
</strong></h5>
