		Desde el 2015, el primero de octubre se celebra el Día Internacional del Café, fecha en la cual se busca rendir un homenaje al grano y todas aquellas personas que trabajan en el campo produciendo una de las bebidas más tomadas en el mundo.
La Organización Internacional del Café, quien ha institucionalizado esta fecha, busca visibilizar más este producto, fomentar su consumo y aportar  a la dinamización de la economía de los productores.<br><br>

“Hoy celebramos un día muy especial e importante, queremos agradecer y festejar con las familias que día a día luchan por darle un valor agregado a sus productos”, expresó la secretaria de Agricultura y Desarrollo Rural, Carolina Gómez Ramírez.  
Entre los aportes, programas y apoyos que el Gobierno de Caldas, en convenio con el Comité Departamental de Cafeteros de Caldas, ha entregado para este gremio están los apoyos en sostenibilidad, incentivos para la modernización caficultora y la entrega de equipos como despulpadoras para el proceso cafetero. Además del programas ‘Mujeres Cafeteras sembrando futuro’.<br><br>
La cifra:<br>

Según el Sistema de Información Cafetera, a 31 de diciembre del 2018,  en Caldas hay 32. 685 personas que se dedican a la producción del Café y hay sembradas 66.000 hectáreas del grano a lo largo de 25 municipios.
