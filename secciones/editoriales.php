	  <p> Plan de Desarrollo: adiós a la Reforma Rural Integral
La política rural del plan denominada “Campo con progreso:
una alianza para dinamizar el desarrollo y la productividad
de la Colombia rural” está incluida en el pacto del emprendimiento y no en el de la equidad.
Por Mauricio Uribe López * escuela nacional sindical E.N.S.
El plan de desarrollo propuesto por el presidente Iván Duque
Márquez contiene un conjunto de “pactos” que no han sido
discutidos ni acordados con actores diversos y por tanto, no
merecen ser calificados como tales. Hay, además de unos
“pactos transversales” y “regionales”, tres “pactos estructurales” que son la base del plan: por la legalidad, por el emprendimiento y por la equidad. La carta de navegación del
gobierno corresponde, ciertamente, a lo que se conoce como
publicidad engañosa puesto que en su título “pacto por la
equidad” hay dos términos carentes de verosimilitud: pacto y
equidad. Además de no ser resultado de un acuerdo social,
el plan carece de los recursos institucionales, financieros y
técnicos orientados hacia la implementación de una agenda
redistributiva. El plan no contiene propuesta fiscal alguna basada en la progresividad del gasto y de la tributación. Tampoco incluye medidas comprometidas con la democratización
de los activos o el derribamiento de las barreras de clase. El
nombre del plan se parece al del partido del presidente: tampoco ahí, los términos son verosímiles.
En lo que tiene que ver con el campo colombiano, la propuesta gubernamental de desarrollo carece por completo de
una perspectiva de equidad en la medida en que privilegia
los negocios agroindustriales y desconoce la relevancia socioeconómica, política y ambiental de la economía campesina. De hecho, la política rural del plan denominada “Campo
con progreso: una alianza para dinamizar el desarrollo y la
productividad de la Colombia rural” está incluida en el pacto
del emprendimiento y no en el de la equidad. Ahí vuelve y
juega la falta de precisión: ¿alianza entre quienes? Quizá ese
enunciado sea menos inverosímil si se completa la oración:
alianza contra los campesinos.
Ese desequilibrio entre agroindustria y economía campesina va en contravía de lo planteado en el primer punto del
acuerdo de paz con las Farc, la Reforma Rural Integral. En
el acuerdo se reconoce la importancia de la coexistencia entre distintas actividades económicas y modalidades diversas de producción agropecuaria, entre la agroindustria y la
economía campesina. Sin embargo, el plan borra de plano
esa coexistencia al ignorar por completo las necesidades y
aportes de esta última. La Misión para la Transformación del
Campo advirtió claramente que 63 por ciento de los agricultores familiares no tiene acceso a alguno de los siguientes
activos productivos (además de la tierra): asistencia técnica,
crédito y riego intra-predial. A pesar de esas adversidades, la
agricultura familiar genera más de la mitad de la producción
agropecuaria nacional. En otras palabras, son mucho más
eficientes nuestros campesinos que los inversionistas que
dedican grandes extensiones de tierra a cultivos de tardío
rendimiento. Estos inversionistas son, además, beneficiarios
de generosas exenciones que el plan de desarrollo mantiene
intactas.
El hecho de que el plan favorezca en forma desproporcionada a la agroindustria hace dudar de sus posibilidades de
inclusión social y productiva. El esquema de las cooperativas
de trabajo asociado en el que se suelen basar las cadenas
productivas tiende a plantear una relación de monopsonio
entre campesinado y agroindustria. Adicionalmente, los inversionistas en cultivos de tardío rendimiento (al igual que
las multinacionales mineras), rara vez se sienten obligados a
incorporar los estándares de empleo decente de la OIT.
El tradicional sesgo anti-campesino de nuestro estilo de desarrollo se intensificará con la ejecución de este plan. El campesinado brilla por su ausencia en el documento. De hecho,
la palabra “campesino” aparece muy pocas veces en el texto.
El bienestar de los campesinos se menciona sólo en el objetivo 15 relacionado con seguridad y convivencia ciudadana,
en el que se señala que el Sistema Integrado de Seguridad
Rural tiene como propósito “velar por el bienestar del campesino” (pág. 35).
Las zonas de reserva campesina no existen en el documento. Tampoco el fondo de tierras.
En el acápite “campo con progreso” la palabra campesino
apenas aparece cuando se hace mención de los “mercados
campesinos”. Las zonas de reserva campesina no existen en
el documento. Tampoco el fondo de tierras. Aunque menciona
la importancia de la institucionalidad para lo rural, el gobierno
mantiene a la dirección de la Agencia de Renovación del Territorio en la interinidad. A cargo de la Agencia de Desarrollo
Rural hay una saboteadora de los acuerdos de paz.</p>


