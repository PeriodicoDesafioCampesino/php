	  ayudó a poner en marcha la iniciativa de traslado de región para algunos de ellos.
	  Además de licenciado y especialista en Ciencias de la Educación, ha sido docente de materias agropecuarias y fue delegado de los presidentes
	  Barco y Gaviria en la Junta Directiva del Incora y asesor del ministro Gabriel Rosas Vega en la Oficina de Asuntos Campesinos.
	  En entrevista con Vanguardia habló sobre la situación del agro santandereano y aseguró que si no se toman acciones urgentes, los
	  campesinos no van a aguantar más.</br> </br>
	  <strong>¿Qué tanto ha cambiado la agricultura campesina en Santander, con respecto a la de hace 30 años?  </strong> </br>
	  
	  El desarrollo de la economía campesina en Santander en los últimos años no es muy positivo. Prácticamente, hace más de 30 años que no hay una
	  política consistente para el desarrollo rural en el país. En su momento, la iniciativa más importante en cuanto a política de desarrollo rural 
	  fue en la década de los 80 con el DRI (Fondo de Desarrollo Rural) que fue muy bueno, con una visión integral del desarrollo del campo. 
	  Después de eso se han dado bandazos de ciego en cuanto a política agropecuaria. Sedesmontó la institucionalidad y los posibles reemplazos de ese
	  vacío no han sido consistentes. Todo se concentró en la agencia de tierras. Desde el punto de vista institucional no hay nada porque unas son las 
	  políticas que se trazan a nivel nacional, otras a nivel departamental y otros son lo esfuerzos a nivel municipal. Hay acciones aisladas, descoordinadas,
	  más respondiendo a protagonismo político y agenda electoral que a desarrollo. </br> </br>
	  
	  <strong>¿Y a nivel de producción? </strong> </br>
	  
	  Desde el punto de vista de resultados es lamentable. Por ejemplo, hace años se cultivaba maíz y era la fuente principal de subsistencia,
	  se sembraba dos veces al año, se recogían la cosecha, se alimentaban las familias y además habían excedentes comercializables, pero decidieron
	  que era mejor importarlo. Se producían cerca de 15 mil toneladas de fique en Santander, se hilaban y tejían 19 millones de empaques para tubérculos
	  y hortalizas y cuando llegó la crisis y metieron el plástico, miles de campesinos aguantaron física hambre y el Gobierno se lavó las manos y
	  volteó la crisis sobre los campesinos. Ahora el plástico es un problema y quieren volver a las fibras naturales, ¿cómo va a uno a motivar a un
	  campesino para sembrar fique si ya tuvo hambruna durante una década y
	  nadie se acordó de ellos?</br> </br>
	  
	  <strong> ¿Lo mismo está pasando con el tabaco?</strong></br> 
	  
	  Hace 40 años Santander era tabacalero por excelencia, era el motor de desarrollo y ahorita prácticamente está acabado. ¿Cómo es posible una ley
	  del tabaco en la que sean los campesinos los que tengan que crear un fondo y se les cobre un gravamen para sostener sus organizaciones, para
	  sostenerse ellos y para hacer proyectos? Eso es como pedirle a un anémico o hemofílico que done sangre. El gravamen no debería ser para
	  los campesinos, sino para los importadores, consumidores y exportadores, pero no, el Gobierno volcó, en una maniobra hábil, todo sobre los
	  campesinos y los puso a cotizar. <br><br>

	  <strong> Entonces, según eso, ¿el agro en Santander seguirá en deterioro?</strong><br>
	  Han habido pequeñas acciones, pero los campesinos hoy en día son más
pobres que antes. El problema de la tenencia de tierra no se ha resuelto, el
minifundio aquí es impresionante. El promedio de tenencia de tierra entre
los minifundistas es de 2.5 hectáreas y es muy difícil que una familia
sobrevivan con eso, sobre todo cuando no ha podido desarrollar tecnología
ni articular mercados. El escenario es bastante crítico, con el agravante que
los últimos gobiernos han satanizado las organizaciones sociales, han
perseguido a los dirigentes campesinos, no quieren trabajar con ellos y el
modelo de desarrollo rural es sesgado hacia grandes empresarios. Y, ¿qué
va a pasar con los 12 millones de campesinos que seguimos arañando la
tierra y tratando de sobrevivir? No sé cómo hemos sobrevivido a ese
fenómeno de autoexplotación. La familia campesina cada día es más pobre
y, sin embargo, seguimos aportando el 40% de los productos de la canasta
familiar.<br><br>

<strong>Y ¿qué pasó con los movimientos campesinos? </strong><br>
Hace unos años leí un artículo de alguien que se atrevía a decir que el
Estado con respecto a las organizaciones campesinas actuó más como
disociador que como aglutinador, porque muchas organizaciones se
crearon, promovieron y financiaron desde el Estado, entonces les costó
mucho construir autonomía. Hubo muchos liderazgos a nivel nacional, pero
no fue posible aterrizarlos a nivel local. No han sido capaces de articular
entre sí las regiones para ver potencialidades y fijarse metas colectivas.
Cada municipio hace cosas y los políticos crean asociaciones, pero luego
se van y llega otro y crea otra y se forman un poco de asociaciones de
papel, según intereses del dirigente de turno. Eso, más los fenómenos de
violencia, han roto el tejido social del campo.<br><br>

<strong>¿Cuál podría ser la solución para los tabacaleros, por ejemplo?</strong><br>

Hay que mirar qué lo reemplaza; afortunadamente, han aparecido
economías alternas que han permitido la supervivencia de campesinos. El
café en Santander está repuntando, por ejemplo. Yo sí creo que el maíz
sería un salvador. Si el Estado hiciera un estudio muy serio de lo importante
de volver a producir maíz, yo creo que habría una gran propuesta que
ayudaría a levantar el agro.<br><br>


<strong>Pero, ¿eso funcionaría para todos?</strong><br>

No para todos. Hay que caracterizar el tabacalero nuestro. La mayoría son
minifundistas, están trabajando suelos agotados, inclusive suelos
salinizados con muy pocas posibilidades de desarrollarse. Algunos son
aparceros. Entonces unos podrían desarrollarse donde están, con cultivos
alternativos como fríjol, maíz, café. Otros hay que apoyarlos para que
salgan y se reubiquen en otro lugar.<br><br>
Hace 30 años hicimos un trabajo muy grande con el Idema, logramos
cultivar frijol como sustituto del tabaco para hacer rotación de cultivos y
mejorar el suelo. Logramos 15 mil toneladas de fríjol ese año y eso fue un
despegue económico buenísimo. Luego llegó la apertura económica y
acabó con eso porque era mejor el frijol importado.<br><br>


<strong>¿O sea que la economía campesina no es viable en un modelo de libre
comercio?</strong><br>
No lo es en las condiciones del país, donde el campesino está a la deriva
y no está en la capacidad de competir con esos mercados, ni en tecnología,
ni en educación, ni en nada. No tenemos soberanía alimentaria, somos
dependientes altamente de las importaciones porque los tratados de libre
comercio fueron sesgados y no respondieron a las necesidades de los
campesinos ni del país.<br><br>

