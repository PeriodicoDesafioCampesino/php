	  <div>
	  
		<img src="../img/agosto2020/fumigacion.jpg" alt="Esto es una foto" id="imgact" ></br>
		<font size="4">
		<p>
			Si bien el ministro de Defensa, Carlos Holmes Trujillo, ha dicho que es inminente, recientes decisiones judiciales ponen en entredicho que esa política se vaya a reanudar pronto. La más reciente suspendió la audiencia pública ambiental necesaria para la aprobación del Plan de Manejo Ambiental del programa de aspersión. </br></br>

Luego de la visita del presidente Iván Duque a Samaniego (Nariño) durante el pasado fin de semana, a raíz de la masacre perpetrada en ese municipio el sábado 15 de agosto en la que murieron ocho jóvenes, el lunes a primera hora el ministro de Defensa, Carlos Holmes Trujillo, convocó a una rueda de prensa. Allí, además de referirse a las cifras y a la controversia alrededor de las masacres o los “homicidios colectivos”, como los han llamado desde el Gobierno, envió otro mensaje: su ministerio está listo operacionalmente para reanudar la aspersión aérea de glifosato contra los cultivos de coca en el país y ello tendría un impacto favorable, según dijo, sobre las masacres.</br></br>

“Hay que decir con claridad, cumpliendo todos los requisitos establecidos por la Corte Constitucional, que la aspersión aérea hoy es más necesaria que nunca para continuar con la disminución de los cultivos ilícitos (…) Reiniciarla es absolutamente indispensable porque su reiniciación tendrá además un efecto positivo sobre este asunto de los homicidios colectivos”, aseguró el ministro.</br></br>

Sin embargo, para quienes han seguido de cerca el procedimiento para reanudar la aspersión aérea con glifosato en el país, suspendida por el Consejo Nacional de Estupefacientes en 2015 (luego de que la Organización Mundial de la Salud clasificara ese herbicida como probablemente cancerígeno) las declaraciones del jefe de esa cartera parecen más un asunto político que busca responder al fragor de la coyuntura. Pero para que la fumigación se reactive aún falta trecho, de acuerdo con la sentencia T-236 de 2017 de la Corte Constitucional y recientes fallos judiciales que se han pronunciado sobre la necesidad de participación de las comunidades afectadas en ese proceso.</br></br>

El más reciente embate que sufre el Gobierno en su intención de retomar esa política lo sufrió este martes por cuenta de la decisión de un juzgado de Pasto de abrirle incidente de desacato al subdirector de Mecanismos de Participación Ciudadana de la Autoridad Nacional de Licencias Ambientales (ANLA), al director de la dirección Antinarcóticos de la Policía Nacional y al director de la Autoridad Nacional de Consulta Previa del Ministerio del Interior. Acción que tomó dicho juzgado al considerar que esas tres autoridades no han cumplido con las órdenes impartidas en dos fallos de tutela de primera y segunda instancia relacionados con el procedimiento para reanudar la aspersión aérea. En ese mismo auto, el juzgado de Pasto ordenó suspender una audiencia clave para el cumplimiento de los requisitos que viabilizarían la aspersión.</br></br>

		
		
