	  <p> 
	  	Se trata de la audiencia pública ambiental necesaria para la aprobación por parte de la ANLA del Plan de Manejo Ambiental (PMA) para el Programa de Erradicación de Cultivos Ilícitos mediante la Aspersión Aérea con Glifosato. Audiencia que en principio había sido convocada, de manera virtual por la pandemia, para el pasado 27 de mayo. Sin embargo, a través de varias tutelas, organizaciones campesinas de Nariño y 88 organizaciones sociales, indígenas y jurídicas, como la Coordinadora de Cultivadores de Coca, Amapola y Marihuana (Coccam), el Colectivo de Abogados José Alvear Restrepo (Cajar), el Cinep o la Comisión Nacional de Territorios Indígenas, lograron frenarla argumentando que de manera virtual no se garantizaba la participación efectiva de las comunidades afectadas. El Juzgado Segundo Administrativo de Pasto les dio la razón y suspendió ese trámite del PMA. Luego, en segunda instancia, el Tribunal Administrativo de Nariño confirmó el fallo y vinculó además al Ministerio del Interior para que ejecute los procesos de consulta previa que sean necesarios con las comunidades étnicas de los 104 municipios en donde se pretende iniciar la aspersión con el herbicida. </br></br>

Sin embargo, luego de ese fallo, en una actuación cuestionada por quienes han intervenido en el proceso, a finales de julio la ANLA publicó un edicto en el que convocaba de nuevo la audiencia pública para el próximo primero de septiembre, aludiendo a un protocolo que consideraba más participativo con las comunidades. Ahí vinieron las advertencias de distintos sectores que dijeron que ese procedimiento no tenía luz verde. </br></br>

“Es muy preocupante que en el afán que tiene el Gobierno de recuperar el control de esos territorios vaya a creer que el cumplimiento de los requisitos en cuanto a la participación ya se ha dado, cuando para la Procuraduría está muy claro que no es así”, explica Diego Trujillo, procurador delegado para Asuntos Ambientales y Agrarios. “Esos fallos han sido claros y han salido a defender el derecho a la participación de las comunidades rurales, étnicas y campesinas, y esos fallos hay que respetarlos”, agrega.</br></br>


Fue por no cumplir con esos fallos que el juzgado de Pasto emitió este martes el auto en que abrió incidente de desacato contra los funcionarios del ANLA, de la Policía y del Ministerio del Interior y suspendió la audiencia del primero de septiembre. Pero el asunto de fondo sobre el que hizo énfasis el juzgado, reafirmando lo dicho en el fallo de segunda instancia, es la necesidad de ejecutar procesos de consulta previa para lograr la licencia ambiental del programa de aspersión con glifosato.</br></br>

En el trámite del PMA, el Ministerio del interior ha defendido su resolución 001 de marzo de 2020, en la que establece que la aspersión no se realizará en territorios de comunidades étnicas por tanto no se requiere consulta previa. “Sin embargo, las organizaciones analizaron los seis núcleos geográficos (donde está proyectado fumigar) y la focalización por municipios y lo que han dicho es que sí hay un cruce o con territorios étnicos ya titulados o con territorios por titular”, explica Isabel Pereira, coordinadora de Política de Drogas de Dejusticia. En ese sentido, la decisión de este martes del juzgado de Pasto dice que entre las entidades territoriales, la ANLA y la Policía deberán rastrear la presencia de comunidades étnicas en zonas de influencia del programa de aspersión e informar de inmediato a la autoridad de consulta previa para que ejecute dicho proceso. Ese sólo trámite podría frenar que se inicien las aspersiones aéreas este año.</br></br>

