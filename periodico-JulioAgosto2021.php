			Amplia los apoyos en subsidios y créditos para las micro y pequeñas empresas, así como mejorar y crear diversas medidas para la generación y sostenimiento de empleos y así contribuir en la reducción del desempleo que ha dejado el covid. </br></br>

			<strong> 5-APOYOS PARA LA REACTIVACIÓN DEL SECTOR AGROPECUARIO.  </strong></br>

			Mejora las medidas de financiamiento para la reactivación del sector agropecuario, alivios financieros adicionales para deudores del sector agropecuario, créditos de emergencia para la reactivación del sector, y establecimiento de un fondo para la estabilización de los precios de los productos agropecuarios. </br></br>

			



