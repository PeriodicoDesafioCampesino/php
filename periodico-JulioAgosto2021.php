			<strong> 1.RENTA BASICA DE EMERGENCIA. </strong> </br>

 			Crea una renta básica de emergencia de un salario mínimo mensual legal vigente por 12 meses para 7,5 millones de hogares que se encuentran en la pobreza y que han sido especialmente afectados por la crisis económica que vive el país. Dicha Renta Básica de emergencia cuenta con enfoque de género y con medidas de veeduría y participación ciudadana. </br></br>

