		<h5 class="header col s12 light " > <strong>
			
			¿Adiós a la mercantilización de la salud?

		</strong> </h5>
	  </div>
	  
	  
	  <font size="4">
	  <p> 

	  	El paso de la reforma a la salud en el Congreso no ha sido fácil. De hecho, su discusión ha sido aplazada 18 veces. ¿Qué callos pisa esta iniciativa? La App Conexión Congreso habló con el representante a la Cámara de Representantes por Caldas del Partido Liberal, José Luis Correa López, médico que ha hecho estudios y pasantías en cirugía cardiovascular y en cirugía de trauma, en el hospital de Pittsburgh en los Estados Unidos y autor principal del proyecto.</br>
			Según el congresista, el proyecto 062 de 2018 (Cámara) ‘por el cual se redefine el sistema general de seguridad social en salud y se dictan otras disposiciones’, busca darle un revolcón a este sistema enfermo y romper con la mercantilización de la salud.</br>
			Correa López entregó a la App Conexión Congreso un diagnóstico y un remedio para el sistema de salud:</br></br>

			<strong> El diagnóstico </strong>  </br>

			Para el médico congresista, el 83% de las EPS en Colombia no funcionan. “Seguir defendiendo un sistema de salud como el que tenemos hoy en día, porque el 17% de las EPS sí funcionan, es decirle a 35 millones de colombianos que les toca morirse para que otros 10 millones de colombianos tengan una atención en salud digna”. 	</br>

			Correa considera que en el país existen colombianos de primera y segunda categoría. ¿Qué mensaje es el que le estamos enviando al país? “El gobierno nacional piensa que el sistema debe continuar como está, de hecho se ha opuesto radicalmente a  este proyecto de ley, pero nosotros seguimos defendiéndolo de manera férrea, porque consideramos que las EPS son negocios, lo que buscan hoy es cambiar lágrimas y muertos por dinero, y no cumplen con ese papel para que las crearon”, le dijo a Conexión Congreso.	</br>

			En términos económicos, el congresista resume el diagnóstico: “El problema del sistema de salud es que el dinero no fluye, no hay plata donde tiene que estar para atender la gente”.	</br>

			El círculo vicioso consiste en que las EPS no les pagan a los hospitales (IPS) y no les entregan los medicamentos a los usuarios, porque éstas no les pagan a los distribuidores de medicamento. Al recibir el dinero de los contribuyentes, estas generaron una ‘mercantilización de la salud’, en donde ellas tienen el sartén por el mango a la hora de contratar y pagar.	</br>

			Y por ello, aclara, “a uno no lo atienden en un hospital o en una clínica, no porque el médico no lo quiera atender o porque tenga pereza, no porque la EPS esté llena, sino porque no tiene contrato con esa entidad, y eso es lo que vuelve nefasto al sistema”. 	</br> </br>

			<strong> El remedio</strong>  </br>

			El proyecto 062 busca romper con la mercantilización y que el Estado recupere esa plata. “Queremos quitar esa plata a las EPS para que ellos dejen de tomar las decisiones del sistema de salud, les queremos quitar esa potestad, ese abuso, ellos contratan, ellos auditan, y deciden cómo se paga y cuando se paga, eso es lo que tiene colapsado el sistema de salud”, explicó el representante Correa. </br>

			De acuerdo con lo planteado en el proyecto de reforma al sistema de salud, el dinero no se va a girar como sucede actualmente: “Las EPS tienen unas cuentas maestras donde manejan el dinero, la plata va a la Adres (Administradora de los Recursos del Sistema General de Seguridad Social en Salud) y se queda ahí hasta que no se ejecute. Cuando se ejecute el dinero va a quién lo ejecutó, que son los prestadores. Vamos a tener unos interventores del sistema (proyecto de ley), que en ningún momento van a  ser intermediarios, porque no van a tocar el dinero del sistema (Gestoras Integrales de Salud)”. </br>

			El proyecto contempla pagarle lo mismo que se les paga a las EPS para poder redistribuir el riesgo de salud. “Que ellos tengan la obligación de responder por unos indicadores en salud, y que dependiendo de que tan bien lo hagan les pagamos un 5% del UPC y por organizar el sistema les pagaremos el 3%.”, explicó Correa.</br>

			Además, aclaró que las EPS no van a  tener los poderes absolutos como actualmente lo tienen, y con ello,  se va a garantizar que haya más actores en el sistema para que sea un sistema más limpio y fácil de vigilar.</br>

			“Hoy, las EPS pueden contratar con quien quieran, y como quieran, y son los únicos que los definen. Y de hecho hoy, existen contratos con cláusula de terminación unilateral en los convenios. Ejemplo: yo soy una EPS y contrato contigo, que eres una clínica. Si yo consigo una clínica que me dé un mejor precio, así me dé un peor puedo romper el contrato aquí y me voy para allá. Eso que significa que no hay seguridad jurídica para los prestadores” señaló el representante.</br>





