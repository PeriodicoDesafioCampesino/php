		Son un poco más de 1700 familias cafeteras que en 5100 hectáreas de café colocan a Manizales como la primera ciudad capital productora de café del país,
		haciendo que el cultivo de este grano se convierta en el principal renglón de la económica agrícola de la capital caldense con un poco 
		más de 61.000 millones de pesos cada año en productividad.</br></br>

