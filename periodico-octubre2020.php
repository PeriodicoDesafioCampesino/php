	   <h5 class="header col s12 light center"><strong> Minga Indígena: Los Obispos de Colombia llaman a un “auténtico diálogo social”
</strong></h5>
		</div>
		<h5 class="center"><img src="img/octubre2020/minga3.jpg" alt="Esto es una foto" id="imgact" ></br></h5>
		
		<font size="4">
		<p>  
		<b>Elaborado por: Eliécer Ortega C</b></br></br>
		
		Producto de las recientes movilizaciones indígenas desde el Cauca reclamando terminar el asesinato de líderes indígenas y sociales, 
		además de peticiones como adjudicación de tierras, ampliación de resguardos, para los campesinos asignación de baldíos, entre otros, la Minga que llegó a Bogotá 
		y participó activamente del paro Nacional del 21 de octubre/20, exigió al gobierno del Presidente Duque reunión para debatir en torno a cuatro puntos: </br></br>
		
		1.	Exigen garantías, el desmonte paramilitar y la reforma a la doctrina militar de seguridad nacional y por el respeto a la vida.</br>
		
		2.	Respeto a las tradiciones y cultura territorial, afectado por los modelos de extracción minera y de recursos naturales. Se oponen a  la política 
		extractivista que profundiza los conflictos entre indígenas, campesinos y afros con las multinacionales por la apropiación de los recursos naturales.</br>
		
		3.	La exigencia del cumplimiento a un Estado Social de Derecho, inspirado en la Constitución Política de1991, como un gobierno democrático con garantías 
		para la participación social y política.</br>
		
		4.	Exigen el cumplimiento de los Acuerdos de Paz de la Habana y acciones concretas frente a la reactivación de los conflictos armados y los asesinatos 
		de excombatientes comprometidos con la construcción de la paz.</br></br>
		
		Fueron cerca de  7500 indígenas que actualmente conforman 126 cabildos de 10  pueblos indígenas vinculadas a la movilización que de acuerdo con los promotores del paro, 
		maestros, centrales obreras, campesinos, trabajadores del sector informal, desfilaron pacíficamente desde diferentes puntos de la ciudad de Bogotá hasta
		la Plaza de Bolívar.</br></br>
		
		La soberbia del Presidente de la República no hizo posible la reunión exigida, pero en la historia quedó constancia.</br></br>
		
		Ante esta situación los obispos de Colombia hicieron llamado al  gobierno nacional, a las instituciones públicas y privadas para que realice un dialogo 
		social auténtico como lo expresaron el 18 de octubre pasado en el comunicado de la Conferencia Episcopal de Colombia-CEC; como lo expresado por 
		el Papa Francisco en su 3ª. Encíclica Fratelli Tutti, “es el camino más adecuado para llegar a reconocer aquello que debe ser siempre afirmado y respetado,
		y que está más allá del consenso circunstancial”. Insisten en evitar que se desvíen los propósitos verdaderos de las movilizaciones, es importante que 
		“se defienda la determinación de los participantes en la Minga”.</br></br>
		
		Otros aspectos importantes se destacan en el comunicado de la CEC, como:</br></br>
		•	Defender la determinación de los participantes en la Minga para que las marchas y manifestaciones se realicen en forma pacífica, 
		evitando que se desvíen sus legítimos propósitos o que degeneren en hechos de violencia.</br>
		
		•	Reconocen  y agradecen los gestos de solidaridad que han ofrecido a la Minga las autoridades y un sinnúmero de personas, 
		especialmente las iniciativas destinadas a proteger la vida y la salud de los manifestantes.</br>

		•	Acercarnos a los rostros de indígenas y afrocolombianos, quienes, en muchas ocasiones, no son tratados con dignidad e igualdad de condiciones, 
		y no siempre han encontrado espacios para expresar la riqueza de su cultura y la sabiduría de su identidad (cfr. Benedicto XVI, Discurso inaugural en Aparecida).</br>
		
		•	Se comprometen en la tarea de crear conciencia social acerca de la realidad de los pueblos indígenas y sus innumerables valores, impulsar el 
		reconocimiento pleno de sus derechos individuales y colectivos, y acompañarlos especialmente en el fortalecimiento de sus identidades y 
		organizaciones propias, la defensa de sus territorios, el acceso a la educación intercultural y el ejercicio de sus derechos ciudadanos.</br></br>
		
		Llaman la atención sobre el propósito de los marchantes, que no es otro que buscar caminos en la defensa de la vida, del territorio y la paz. </br>
		Finalmente, los Obispos insisten en la necesidad de crear una verdadera cultura del encuentro fraterno “que nos permita abrirnos a los hermanos, 
		descubrir la riqueza de la diversidad, sanar heridas, tender puentes y abrir caminos para la convivencia en la justicia y en el bien común”.</br></br>


