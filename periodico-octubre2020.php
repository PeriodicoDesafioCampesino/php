		El componente ambiental comprende la adecuación de pozos sépticos mejorando las condiciones de disposición final de aguas negras de las fincas.  
		Adicionalmente se realizará la construcción de 101 casetas sanitarias y reparación a otras que requieran dicho proceso.</br></br>

