	  <h5 class="header col s12 light"><strong>CON 29 PROYECTOS, CALDAS PARTICIPÓ EN CONVOCATORIA DE MINAGRICULTURA
	  SOBRE ALIANZAS PRODUCTIVAS PARA LA VIDA
</strong></h5>
	  </div>
	  <img src="img/peces-cafe-vacas.png" alt="Esto es una foto" id="imgact" ></br>
	  
	  
	  <p> 
	  Con 29 perfiles de proyectos productivos provenientes de 14
municipios del departamento, Caldas participó en la convocatoria
Proyecto Apoyo a Alianzas Productivas para la vida, del Ministerio
de Agricultura. La totalidad de proyectos presentados por la región
asciende a un valor cercano a los 10 mil millones de pesos, los
cuales buscan atender a cerca de 500 productores agropecuarios
del departamento, de los cuales alrededor del 52% son mujeres
rurales.
La secretaria de Agricultura y Desarrollo Rural, Ana Milena Nieto
Ossa, indicó que el Proyecto Apoyo a Alianzas Productivas para la
Vida es un instrumento que vincula a pequeños productores rurales
con los mercados, a través de un esquema de agronegocio con un
aliado comercial formal, con una propuesta productiva rentable,
sostenible y competitiva. Se busca fortalecer el eslabón más débil de
la cadena: la comercialización.
La funcionaria indicó que esta primera etapa de la convocatoria
2020, que cerró el pasado 13 de junio, pretende beneficiar a
pequeños productores rurales y sectores afines a la producción
agropecuaria. Agregó que el programa, según indicaciones del
Ministerio, prioriza la participación de las mujeres y jóvenes rurales,
con el objetivo de garantizar la participación de estas poblaciones y
disminuir la migración del campo a la ciudad.
“El proceso continúa en evaluación de la Gobernación, mediante la
Secretaría de Agricultura, donde se priorizarán siete proyectos que
serán presentados al Ministerio de Agricultura y Desarrollo Rural.
Una vez que el Ministerio los viabilice, pasarán a la etapa de
preinversión y posteriormente a la etapa de inversión para su
respectiva materialización”, manifestó la funcionaria.
Finalmente, Nieto Ossa resaltó la alta receptividad de los caldenses
en la presentación de estas iniciativas durante la primera
convocatoria del Proyecto Apoyo a Alianzas Productivas para la
vida. Indicó que todos los proyectos presentados serán almacenados
en un banco de perfiles, con el fin de tenerlos en cuenta en
posteriores convocatorias del mismo Ministerio u otras entidades.
