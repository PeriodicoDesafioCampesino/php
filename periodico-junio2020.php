emergencia para el sector agropecuario, pesquero y de desarrollo
rural, abordan tres aspectos fundamentales para la dinámica del
sector: i) la continuidad del suministro y abastecimiento de
alimentos, ii) el fortalecimiento de la oferta de financiamiento, y iii)
apoyos y seguridad alimentaria, entre otras.</br></br>
