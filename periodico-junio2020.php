		<font size="4">
		<p> Nosotros, campesinos y campesinas, comunidades étnicas, pescadores y pescadoras artesanales, ciudadanos, ciudadanas y comunales colombianos, 
		hacemos un llamado para que el país ponga la Economía Campesina, Familiar y Comunitaria en el centro de las soluciones de la emergencia que atravesamos. 
		La coyuntura actual hizo evidente que hemos postergado la solución de problemas estructurales de nuestra sociedad.</br> </br> 
		Frente a la pandemia del COVID-19, seríamos menos vulnerables a una crisis alimentaria si contáramos con avances significativos
		en el cumplimiento de los compromisos asumidos por el Estado colombiano en diferentes procesos sociales durante más de 20 años.</br> </br> 
		Ello implica asumir con determinación la reforma rural integral, la sustitución de cultivos de uso ilícito y otros puntos centrales del 
		Acuerdo Final para la Terminación del Conflicto y la Construcción de una Paz Estable y Duradera; el reconocimiento del
		campesinado y a quienes trabajan en las zonas rurales en tanto sujetos especiales de derechos -conforme a la Declaración de las
		Naciones Unidas-, la interculturalidad, y el desarrollo de las diversas formas de territorialidad afrocolombianas e indígenas.</br> </br> 
		La Economía Campesina Familiar y Comunitaria, así como la
étnica, tienen de tiempo atrás, condiciones restringidas de acceso
a la propiedad de la tierra, al crédito, a la asistencia técnica, a la
comercialización, a mares y ríos y a una infraestructura adecuada.</br> </br> 
En Colombia, además de cientos de miles de familias campesinas
sin tierra, existen más de dos millones de predios campesinos sin
formalizar. La situación con la titulación colectiva a comunidades
indígenas y afrocolombianas es igualmente precaria y
preocupante: al año pasado se acumulaban por miles las
solicitudes sin resolver.</br> </br> 
Aun con todas estas dificultades, el campesinado responde aún
hoy por más el 70% de la producción de los alimentos que
consumen los hogares del país (Procuraduría General de la
Nación, Memorando No. 005 del 27 de marzo de 2020). Pero el
panorama es alarmante. La devaluación del peso implicará alza en
precios para los alimentos importados, o que tienen un alto
componente de insumos externos.</br> </br> 
La vulnerabilidad alimentaria no es solamente un problema rural.
Miles de colombianos hoy están pasando hambre en las ciudades.
Por ello es necesario atender las propuestas del campesinado
para construir los puentes entre los productores rurales y los
consumidores urbanos, con prioridad en los sectores más
vulnerables. </br> </br> 
Los impactos del conflicto social y armado, que aún no cesa,
afectan, en añadidura de los intolerables crímenes y la violencia, al
campo y también a las ciudades. La implementación del Acuerdo
de Paz y la reforma rural integral son condiciones necesarias para
garantizar y proteger el derecho humano a la alimentación.</br> </br> 
Reconocer esta realidad permite, asimismo, superar la actual
encrucijada entre "paz-conflicto y pandemia".
</p>
