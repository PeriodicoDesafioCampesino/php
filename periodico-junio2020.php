	  <p> La pandemia y las medidas para su mitigación también impactan y
en alto grado a las comunidades rurales y las familias campesinas,
los territorios y las poblaciones históricamente afectadas por el
conflicto armado, la pobreza, la marginalidad, la presencia de
economías ilícitas y la debilidad institucional. Tanto por los riesgos
a la salud, en territorios carentes de atención médica, como por la
pérdida de ingresos, a causa del debilitamiento y eventuales
restricciones en las cadenas de distribución, abastecimiento y
comercialización de productos e insumos agropecuarios.</br> </br> 
Los previsibles sobrecostos en la cadena de intermediación, así
como el traslado de tasas de usura tanto en los insumos para los
productores rurales como en los alimentos para los consumidores
urbanos, son hoy una amenaza para productores, transportistas,
tenderos y consumidores y específicamente para la realización del
derecho alimentario de toda la sociedad colombiana.</br> </br> 
No es postergando la implementación integral del Acuerdo de Paz
como se financia el camino para atender y salir de esta crisis y
coyuntura. La ruta eficaz y cierta, en el corto y mediano plazo, es
que la sociedad colombiana reconozca y se apropie de la reforma
rural integral y del fortalecimiento de la economía campesina y
étnica como condiciones necesarias para la seguridad alimentaria
de todos y todas.</br> </br> 
Tal y como se estableció, en el Acuerdo Final Para la Terminación
del Conflicto y la Construcción de una Paz Estable y Duradera,
con alcance constitucional a, en el Punto 1.3.4., se requiere contar
con el “Sistema para la garantía progresiva del derecho a la
alimentación: en desarrollo de la obligación de garantizar de
manera progresiva el derecho humano a la alimentación sana,
nutritiva y culturalmente apropiada, con el propósito de erradicar el
hambre y en esa medida fomentar la disponibilidad, el acceso y el
consumo de alimentos de calidad nutricional en cantidad
suficiente, el Gobierno Nacional pondrá en marcha un sistema
especial para la garantía progresiva del derecho a la alimentación
de la población rural. La política alimentaria y nutricional en las
zonas rurales se basa en el incremento progresivo de la
producción de alimentos, la generación de ingresos, y en general
la creación de condiciones de bienestar mediante los planes
nacionales de acceso a tierras, infraestructura, riego, vivienda y
agua potable, asistencia técnica y capacitación, mercadeo, crédito,
la promoción de formas asociativas basadas en la solidaridad y la
cooperación, y demás planes establecidos en el presente acuerdo.
Esta política reconoce el papel fundamental de las mujeres rurales
en la contribución a la satisfacción del derecho a la alimentación”.</br> </br> 
Por todo lo anterior, invitamos a construir un acuerdo nacional,
amplio e incluyente, que ponga en marcha un proceso concreto de
fortalecimiento de la economía y producción campesina rural de
alimentos y garantizar de manera progresiva y sostenible el
derecho a la alimentación de la población rural y poblaciones y
comunidades próximas a las zonas de producción y el adecuado
abastecimiento, comercialización y distribución masiva de
alimentos a los centros urbanos.</br> </br> 
En este propósito, nos sumamos al requerimiento de convocar de
manera urgente una reunión de alto nivel de la Comisión
Intersectorial de Seguridad Alimentaria y Nutricional con las
organizaciones campesinas, y suscribimos las invitaciones a
“Fortalecer la producción campesina de alimentos para la vida y la
paz” y a “Proteger el campesinado para proteger la vida”.
Exhortamos a las nuevas autoridades departamentales y
municipales a incorporar estos postulados en sus Planes de
Desarrollo 2020 - 2023. </br> </br> 
Debemos proteger el campo y la Economía Campesina Familiar y
Comunitaria como la base para la realización del derecho a la
alimentación, de la seguridad y soberanía alimentaria, y de la
recuperación económica del país. Rechazamos la imposición de
medidas que debilitan la producción campesina y aumentan los
riesgos de hambre y malnutrición en Colombia.</br> </br> 
Invitamos a las organizaciones sociales, comunitarias y
empresariales, rurales y urbanas, a la academia y a la ciudadanía
en general en adherir, difundir y apoyar LA CONVOCATORIA.
</p>
