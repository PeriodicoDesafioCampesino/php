suministro y abastecimiento de alimentos se enfocan en evitar y
corregir los diferentes obstáculos que amenazan la dinámica
usual de las cadenas productivas y de comercialización de
alimentos durante la emergencia. Así como en fortalecer los flujos
de información de mercados para que de manera permanente
puedan identificarse las distorsiones que afecten a los agentes de
