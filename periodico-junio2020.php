	  <p> </br></br>
	  En cuanto al fortalecimiento de la oferta de financiamiento, el
Ministerio de Agricultura y Desarrollo Rural tiene a disposición los
instrumentos e incentivos financieros enmarcados en la política de
financiamiento agropecuario para su implementación en todo el
territorio nacional, de acuerdo con la demanda por parte de los
productores, y con la disponibilidad presupuestal de dichos
programas. Dentro de las medidas adoptadas, se encuentran
nuevas condiciones para los créditos como periodos de gracia,
aumento de plazos, entre otros aspectos.</br></br>
Por último, el Gobierno Nacional, con el objetivo de reducir los
impactos sociales y económicos de la emergencia sanitaria que
enfrenta hoy el país, ha tomado medidas para realizar apoyos
directos a poblaciones vulnerables especificas, como por ejemplo
el incentivo económico para la población mayor de 70 años que se
dedica al campo y que se encuentra en aislamiento obligatorio.
También se han adoptado medidas para apoyar a los productores
en temas específicos como la importación de algunos productos e
insumos agropecuarios, recomendaciones frente a la prevención
del coronavirus COVID 19 en las actividades agrícolas y ganaderas
y estrategias de apoyo a la comercialización entre otros.</br></br>
En el documento adjunto a esta comunicación, se comparte la
totalidad de las medidas que ha adoptado el Gobierno Nacional
para el sector agropecuario dentro de la emergencia, las cuales
responden a las inquietudes manifestadas en su comunicación,
específicamente en los temas que competen al Ministerio de
Agricultura y Desarrollo Rural. En todo caso, no se trata de un
documento definitivo, puesto que los diferentes equipos técnicos de
las autoridades del sector avanzan en nuevas medidas, así como
en la actualización de las que ya se han adoptado.</br></br>
Para finalizar, informamos que con ocasión de la emergencia
decretada no es posible que el Señor Ministro de Agricultura y
Desarrollo Rural, Rodolfo Zea Navarro, atienda citas en este
momento. No obstante, reiteramos el agradecimiento por su
comunicación y el compromiso del Gobierno Nacional para seguir
trabajando en la mitigación los efectos negativos que el COVID 19
ha generado en el sector agropecuario de nuestro país.</br></br>
De esta forma damos respuesta a su requerimiento,</br></br>
Cordialmente,</br></br></br></br>
MARCELA URUEÑA GÓMEZ</br>
Viceministra de Asuntos Agropecuarios
