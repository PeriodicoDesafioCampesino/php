	  </div>
	  <img src="img/armas.jpg" alt="Esto es una foto" id="imgact" ></br>
	  
	  
	  <p> 
	  Es claro que los Estados deben respetar, promover y garantizar los
derechos humanos; sin embargo, pero parece necesario recordar al
gobierno de Duque estas obligaciones internacionales.
Desde el 2016, año en que se firmó el Acuerdo de Paz entre el
Estado colombiano y la extinta guerrilla de las FARC-EP, la violencia
contra lideresas y líderes defensores de derechos humanos, lideresas
y líderes de restitución de tierras, líderes sociales, población en
proceso de reincorporación y, en general, las comunidades rurales, se
ha incrementado gradualmente: el año 2019 se documentaron 844
agresiones contra líderes y lideresas sociales, fue el año con mayor
nivel de violencia contra esta población en la última década[1] y,
conforme esta tendencia, entre enero y mayo de 2020 han sido
asesinadas 117 lideresas y líderes sociales junto con 25
excombatientes. Distintos sectores de la sociedad han hecho un
llamado para que el Estado afronte esta problemática, resaltando que
la provisión de seguridad en los territorios no será efectiva si se limita
al aumento de la fuerza. Es claro que una de las estrategias más
fuertes para promover la seguridad de los territorios es el desarrollo
económico y social, ofreciendo a las y los jóvenes oportunidades
económicas para que dejen de vincularse a la guerra.
No obstante, la estrategia del gobierno de Iván Duque se ha enfocado
en la militarización de los territorios, lo cual tiene lugar en el marco del
persistente desconocimiento estatal de los compromisos asumidos en
La Habana. El mejor ejemplo de esto lo constituye la actual estrategia
de lucha contra el narcotráfico. En el Acuerdo Final se reconoce que
la solución definitiva al problema de las drogas ilícitas requiere de
políticas que atiendan las causas y consecuencias de este fenómeno,
especialmente presentando alternativas que conduzcan a mejorar
condiciones de bienestar y buen vivir de las comunidades en los
territorios afectados por los cultivos de uso ilícito. En el mismo
sentido, se reconoce que un elemento indispensable para la solución
definitiva al problema de los cultivos de uso ilícito es el carácter
voluntario y concertado[2], visión a partir de la cual el Acuerdo prioriza
la sustitución voluntaria de los cultivos en el marco del Plan Nacional
Integral de Sustitución de Cultivos de Uso Ilícito (PNIS), permitiendo,
subsidiariamente, actividades de erradicación forzosa.
Según lo estipulado en el Acuerdo de Paz, la erradicación forzosa
sólo procede en tres casos: i) cuando, en el marco de la suscripción
de acuerdos con las comunidades, haya algunos cultivadores y
cultivadoras que no manifiesten su decisión de sustituir los cultivos, ii)
cuando se incumplan los compromisos adquiridos en el marco del
PNIS y iii) cuando no haya acuerdo con las comunidades para la
sustitución. No obstante, presenciamos un descarado
desconocimiento de estas disposiciones por parte del Estado.
El actual gobierno no sólo ha decidido dejar de celebrar acuerdos de
sustitución voluntaria con las comunidades, sino que ha priorizado
operativos de erradicación forzosa. Este tipo de actividades no sólo se
desarrollan en contravía de lo estipulado en el Acuerdo, sino que
además están produciendo graves situaciones de violación de
derechos humanos. Los operativos se han intensificado, en medio de
la emergencia sanitaria producida por la pandemia, en 8
departamentos del país: Caquetá, Chocó, Sur de Córdoba,
Catatumbo, Nariño, Putumayo, Meta y Guaviare. Frente a esto, las
comunidades han salido a protestar, no sólo para defender los cultivos
que constituyen su único medio de subsistencia, sino también para
exigirle al gobierno que cumpla con los compromisos que asumió en
los acuerdos de sustitución celebrados con las comunidades en el
marco del PNIS.
