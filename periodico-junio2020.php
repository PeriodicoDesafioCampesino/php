		<font size="4">
		<p>  
		La Jefatura de Gestión del Riesgo, Medio Ambiente y Cambio
Climático de Caldas se une a la celebración del Día Mundial del
Medio Ambiente y resalta la creación de la Secretaría de Medio
Ambiente y Desarrollo Sostenible en el departamento, con el fin de
lograr un trabajo articulado en este sector. Igualmente se destaca la
adquisición de predios de cuencas hídricas en la región para la
preservación y conservación del agua.
El director de la Jefatura de Gestión del Riesgo, Medio Ambiente y
Cambio Climático del departamento, Félix Ricardo Giraldo Delgado,
resaltó la importancia del segundo pilar del Plan de Desarrollo de
Caldas, relacionado con medio ambiente y desarrollo sostenible.
Asimismo manifestó: “la Secretaría articulará las políticas de orden
nacional, regional y local, con el fin de garantizar que todos los
caldenses vivan en un ambiente sano, conscientes del cuidado de
los diferentes recursos”.
El funcionario agregó que se tendrán acciones con Corpocaldas
como autoridad ambiental y con los diferentes municipios,
encaminadas a la protección de los recursos hídricos, de la flora y de
la fauna, con el fin de garantizarles a las generaciones un territorio
amigable con el medio ambiente.
“Se articularán acciones también con las diferentes secretarías de
Caldas, como Agricultura, Vivienda e Infraestructura, con el
propósito de garantizar que todas las inversiones públicas del
departamento tengan su componente medioambiental que
garanticen la disminución y el mínimo impacto al medio ambiente”,
añadió.
Por su parte, el jefe de la Unidad de Medio Ambiente, Óscar López
Naranjo, destacó la gran riqueza en biodiversidad y ecosistemas de
la región, que acompañada con el recurso hídrico y la variedad de
paisajes brinda la posibilidad de disfrutar al aire libre de este
departamento. López Naranjo mencionó que con la creación de la
Secretaría se trabajará articuladamente con todos los gobiernos
para sacar adelante los proyectos de ecoturismo y aviturismo de
Caldas.
El dato
En Caldas se encuentran 813 especies de aves, 22 especies
endémicas, 73 casi endémicas (*2) según el programa de
investigación “El estado del conocimiento de las aves del
departamento de Caldas” (Ocampo et al. 2018) y aves migratorias
77 especies”. El departamento tiene el 42% de las aves de
Colombia.
