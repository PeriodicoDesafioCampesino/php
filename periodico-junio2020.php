	   <h5 class="header col s12 light"><strong>A los pueblos indígenas los matará el COVID-19 o el hambre si el Estado no actúa inmediatamente</strong></h5>
		 </div>
		<img src="img/amnistia-internacional-logo.jpg" alt="Esto es una foto" id="imgedit" ></br>
		
