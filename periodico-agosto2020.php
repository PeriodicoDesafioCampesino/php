	  <font size="4">
	  <p> 
	  Durante Puesto de Mando Unificado (PMU) con los alcaldes del departamento y diferentes entidades, la Secretaría de Agricultura y Desarrollo Rural de Caldas en conjunto con el Comité Departamental de Cafeteros socializaron el Plan Cosecha 2020.</br></br>

La secretaria de Agricultura y Desarrollo Rural de Caldas, Ana Milena Nieto Ossa, indicó que el Plan Cosecha 2020 es un trabajo en equipo y articulado, con el propósito de garantizar la cosecha cafetera del departamento. “Esta cosecha representa cerca de 720 mil millones de pesos, que impactarán de manera positiva a los 25 municipios cafeteros del departamento, lo que permitirá la reactivación económica de la región ante la crisis por el COVID-19” dijo.</br></br>

La funcionaria indicó que para esta oportunidad se tiene un reto mayor debido a la pandemia, pues para lograr la recolección de esta cosecha se requieren en el departamento cerca de 38 mil 600 recolectores. Indicó que entre las medidas que dispone este Plan Cosecha se contempla el uso de mano de obra local, pero aclaró que aun así existe en la zona Centro Sur y parte del Occidente del departamento algunos municipios donde se requiere de una mano de obra foránea, tal y como ha sido el comportamiento en años anteriores.</br></br>

“Se estima que entre 6.500 y 7.000 recolectores foráneos lleguen a estas zonas del departamento a apoyar la recolección de café, pero en este momento se debe hacer de manera controlada y vigilada. Este también será un trabajo articulado entre alcaldías, gobierno departamental, Comité de Cafetero y los mismos caficultores para evitar la diseminación del COVID-19 a lo largo de la caficultura del departamento”, expresó Nieto Ossa.</br></br>

La jefe de esta cartera también socializó las medidas que el departamento ha definido para lograr el éxito de la cosecha cafetera y preservar la vida de las familias cafeteras, de los recolectores y de toda la población. La funcionaria expresó que las medidas van orientadas a priorizar esa mano de obra local, a tener unas bolsas de empleo en cada municipio y a establecer una articulación permanente con la Policía departamental para acompañar los momentos de pago de los recolectores en un sitio específico. Esto además de las medidas relacionadas con los protocolos de bioseguridad que se deben garantizar en cada una de las fincas.</br></br>

Nieto Ossa agregó que con relación a la población foránea que llega al departamento se va a trabajar también de manera articulada para el seguimiento y recibimiento de estos recolectores, de manera que se pueda caracterizar e identificar el estado de afiliación de cada uno al sistema de seguridad social.</br></br>

“Hay también una propuesta que se viene presentando desde el Comité para que sea ubicado un Centro de Aislamiento en Chinchiná, con el apoyo de diferentes actores, de manera que se pueda apoyar esta iniciativa que busca que los recolectores que no tienen su residencia en Caldas y que de pronto requieren un sitio específico por ser caso sospechoso o positivo asintomático, puedan contar con un sitio especial para su recuperación”, explicó.  </br></br>
Finalmente, Nieto Ossa agradeció a todos los alcaldes por el compromiso, e indicó que se seguirán coordinando las acciones que sean necesarias para garantizar el éxito de la cosecha cafetera 2020, acciones que le apuntan a preservar la vida y la salud de todas las familias cafeteras, al igual que la reactivación económica del departamento.</br></br>

