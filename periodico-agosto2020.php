	   <h5 class="header col s12 light"><strong>Continúan los asesinatos de líderes sociales en Colombia durante la pandemia      </strong></h5>
		 </div>
		 <h5 class="header col s12 center"><img src="img/agosto2020/lideres.jpeg" alt="Esto es una foto" id="imgprincipal" ></h5></br>
		
	  </div>
	
	  
     
<font size="4">
	  <p> 
	  Para mucha gente, la cuarentena representa la primera vez en la vida que su movilidad ha sido restringida. Pero yo ya tengo cuatro años que soy incapaz de caminar a la tienda de mi barrio sola, porque siento que si lo hago me van a matar. Tampoco puedo ir al campo, ni salir a divertirme, pues esto es arriesgar la vida.</br></br>

Esta es la realidad que las y los líderes sociales amenazados llevamos años viviendo en Colombia. Por defender la vida de todas las personas, no podemos disfrutar de la nuestra. Estamos encerrados, moviéndonos de una casa a otra para escondernos, y solo salimos por lo necesario. Sin embargo, en el actual contexto, estar en casa 24 horas todos los días es una sentencia de muerte, ya que los sicarios saben donde encontrarnos. Ahora somos un blanco aún más fácil para quienes quieren silenciarnos.</br></br>

La violencia sistemática que enfrentamos no ha disminuido este año, a pesar de la pandemia. Más de 100 líderes y lideresas sociales y personas defensoras de derechos humanos han sido asesinados en Colombia en lo que va de 2020, incluyendo al menos 28 asesinatos desde que se decretó la cuarentena obligatoria el 25 de marzo para prevenir el contagio del COVID-19. </br></br>

Durante los últimos años he sufrido amenazas de muerte, seguimiento constante y allanamientos. He sido fotografiada en la calle y el año pasado tuve que salir del país por un tiempo cuando se dio a conocer un complot para asesinarme. La violencia que sufro la relaciono con mi labor defendiendo el territorio y los derechos colectivos de las comunidades afrodescendientes de Buenaventura y las denuncias que he hecho a nivel local, nacional e internacional sobre los efectos negativos de la ampliación portuaria en la ciudad.</br></br>

<strong>"Nos siguen matando con mucha facilidad durante la pandemia porque todos estamos en casa cumpliendo con la cuarentena obligatoria"</strong></br></br>

Unos meses antes de la pandemia tuve que abandonar el sitio donde estaba viviendo porque registramos seguimientos que vulneraban mi seguridad. Por eso, me ha tocado estar en una ciudad que no es la mía durante la cuarentena. Estar en un sitio donde no conozco a nadie me genera incertidumbre y una angustia muy grande. No estoy tranquila, sigo muy preocupada por mi situación y la de otros líderes y lideresas sociales que también están muy expuestos en sus territorios.</br></br>

Nos siguen matando con mucha facilidad durante la pandemia porque todos estamos en casa cumpliendo con la cuarentena obligatoria y nadie se puede mover. Pero resulta que las personas que quieren acallarnos sí se están moviendo sin problema. Estamos observando un patrón donde los grupos armados al margen de la ley llegan a las casas de las y los líderes sociales y les asesinan frente a sus familias. Incluso, en algunos casos, matan a sus familiares también.</br></br>

Además, las comunidades han empezado a ver grupos armados repartiendo panfletos con amenazas manifestando que están asesinando a las personas que no acojan la cuarentena y las que supuestamente tienen COVID-19. Están sentenciando a las y los líderes sociales de que no están acogiendo la cuarentena para distraer del real motivo político de los asesinatos. Es como si ahora tuvieran una razón de salud pública para matarnos. En marzo, por ejemplo, la lideresa Yina Paola Sánchez Rodríguez denunció que grupos armados le habían declarado objetivo militar por supuestamente estar contagiando a miembros de su comunidad en el municipio de Montelíbano, Córdoba. La lideresa, quien manifestó estar en perfecto estado de salud, tuvo que huirse del territorio debido a las amenazas.</br></br>

Lamentablemente, ser líder o lideresa social se ha convertido en una de las labores de más alto riesgo hoy en Colombia. Esta situación no solo pone en peligro nuestras vidas, sino también al sistema democrático y participativo del país, porque jugamos un rol importante en el ejercicio de gobernanza y la promoción y defensa del derecho a la participación, y en promover los derechos constitucionales y defender los derechos humanos.</br></br>

Pero en vez de reconocer la importancia de nuestra labor y ver una oportunidad de colaboración, el gobierno nos da la espalda, nos vigila como si fuéramos una amenaza al sistema, y nos trata como criminales. No estamos vinculados a grupos armados, pero tratan de relacionarnos con ellos, generando falsos positivos judiciales. La única arma que usamos es nuestra voz, y nuestra voluntad para aportar en la consolidación de los sueños colectivos de nuestras comunidades. Solo estamos defendiendo nuestros derechos y nos preocupa profundamente que en Colombia se tome nuestro trabajo como si fuera un delito.</br></br>

El gobierno debe tomar medidas urgentes y definitivas para garantizar nuestra seguridad, y llevar a cabo una campaña de no estigmatización a la labor de las y los líderes sociales. La gente debe ser consciente que cada vez que compre comida en un supermercado está siendo beneficiaria directa de nuestra lucha. Las comunidades étnicas ponemos los muertos por proteger un derecho colectivo como es la tierra y el territorio, pero todo el pueblo colombiano goza de los frutos del territorio gracias a nuestra sangre.</br></br>

No podemos seguir ahogándonos en un océano de impunidad. Cada día trae nuevas amenazas y ataques, y se profundizan los riesgos que estamos viviendo. Las medidas de protección implementadas por el Estado no desatiban los riesgos, las autoridades no resuelven los casos y no hacen el seguimiento debido a nuestras denuncias. Parece que son intocables las personas que cometen estos delitos que vulneran nuestras vidas y nuestros derechos a la libertad de expresión, participación y organización, tanto en Buenaventura como en otros territorios estratégicos del país.</br></br>

Las autoridades colombianas también tienen la obligación de garantizar nuestro acceso a la justicia. Deben determinar quiénes son los autores de la violencia que afrontamos y la relación que tienen con el rol político que jugamos las y los líderes sociales. ¿A quién le molesta o a quien le puede perjudicar la labor que hacemos para defender nuestros territorios y los derechos humanos? ¿Quién se beneficia con nuestras muertes?
</br></br>
Danelly Estupiñán es lideresa social y miembro del Proceso de Comunidades Negros (PCN) en Buenaventura</br></br>



