		<font size="4">
		<p> <strong>Por: Eduardo Díaz Uribe</strong> </br>
			Eduardo Díaz Uribe, exministro, describe los riesgos a los que “estamos abocados con la manera débil, desarticulada y parcial con que el Gobierno está implementando los Programas de Desarrollo con Enfoque Territorial y el Programa Nacional de Sustitución de Cultivos -PDETs y PNIS – “. </br></br>

El pasado 4 de mayo, en carta abierta al Presidente Iván Duque, la plataforma “LA CONVOCATORIA por el derecho a la alimentación” conformada por organizaciones campesinas, pescadores artesanales y académicos señalaba cómo cientos de miles de familias campesinas sin tierra, en condiciones precarias de titularidad, con ninguna o muy poca tierra y, por ello mismo, en condiciones restringidas de acceso al crédito, a la asistencia técnica, a la comercialización y a los mercados responden hoy por más del 70% de la producción de los alimentos que consumen los hogares del país. La fragilidad de la economía campesina familiar y comunitaria es la de la seguridad alimentaria y nutricional de todos los colombianos; también lo es del desarrollo del país, regiones y territorios, de la superación del conflicto y las economías ilegales y de la construcción de la paz y la reconciliación.</br></br>

La carencia de titularidad y propiedad de la tierra por parte de las familias campesinas no es tema subsidiario; es sinónimo de pobreza e inequidad en el campo colombiano. La cada vez mayor concentración de la propiedad en pocas manos, producto de la violencia y las contra-reformas agrarias, junto con la ocupación y posesión precarias de la tierra y el proceso de fragmentación de la unidad productiva campesina, tienen para la economía campesina, familiar y comunitaria graves implicaciones en materia de:</br></br>

-Restricciones de acceso al crédito de fomento y a los programas institucionales de apoyo a proyectos productivos por falta de titularidad sobre los predios, condición necesaria para el otorgamiento de garantía para “soportar” el crédito y la inversión pública y privada. Este fenómeno ha sido recurrente y se está viviendo hoy, por ejemplo, en el Programa Nacional de Sustitución de Cultivos -PNIS-, cuando, a pesar de lo definido en el Acuerdo de Paz, las comunidades no pueden acreditar “propiedad” sobre los terrenos en que estaban cultivando coca y donde están sustituyendo.</br>

-Es por esta circunstancia que las familias campesinas han sido y son fácil presa de la especulación de prestamistas y modalidades abusivas y leoninas como el “crédito gota a gota”, los anticipos en especie, los “plantes” y el “trabajo al debe”.</br>

-Estas restricciones también generan a las familias campesinas sin tierra, que son la gran mayoría, inhabilidades para acceder a los programas de subsidios para compra o mejoramiento de la vivienda rural y campesina. Por norma legal, no se pueden invertir recursos públicos en predios cuya titularidad es incierta. El argumento, que esgrimen las autoridades, es el potencial beneficio de terceros que ejerzan o pretendan la titularidad sobre el predio.</br></br>

La fragmentación de los predios campesinos en cada vez más pequeñas unidades productivas ha significado el incremento de la pobreza de las familias, estímulos a la migración a barriadas marginales en centros urbanos o hacia nuevas zonas de colonización y ha hecho fácil presa a la economía campesina familiar y comunitaria de las pesadas redes de intermediación y de procesos de comercialización lesivos a sus intereses: precios bajos para sus productos y precios altos para los insumos.</br></br>

Capítulo aparte merece la revisión de los impactos que en la economía campesina, familiar y comunitaria tienen las inversiones en bienes públicos y vías secundarias y terciarias en los territorios si, de manera previa o simultánea a estas importantes inversiones, no se ha realizado la titulación de predios a campesinos y llevado a cabo, o por lo menos iniciado, la reforma rural y agraria.</br></br>

		
		</p>
		
		</font>
		
	</div>
	  </div>
	  
      <div class="col s6" id="cuadro2">
	  
	  
	  
	  <font size="4">
	  <p> 
	  	A lo largo de toda la historia de nuestro país, incluso la más reciente, la no titulación junto con la no formalización previas de la propiedad de la tierra a las familias campesinas ha propiciado que la “valorización de los predios”, derivada de las inversiones del Estado en vías y bienes públicos, haya dado lugar a que “terceros” por la vía de la violencia, del engaño, de la manipulación de títulos en notarías y juzgados e incluso de la “compra de mejoras”, terminen haciéndose a la propiedad de regiones enteras, expulsando a zonas y regiones marginales, rurales y urbanas, a cientos de miles de familias campesinas, primeras ocupantes y colonizadoras, de los territorios recién incorporados a la economía y los mercados.</br></br>

Éste es el riesgo al que hoy estamos abocados con la manera débil, desarticulada y parcial con que el Gobierno está implementando los Programas de Desarrollo con Enfoque Territorial -PDETs y el PNIS.  Las inversiones que se están realizando en estos territorios, así sean pocas y marginales, deben siempre ir precedidas y acompañadas de acciones que entreguen, vía titulación de baldíos o compra, tierra suficiente a las familias campesinas. No hacerlo es repetir la historia de futuros despojos, nuevos desplazamientos y más colonización de territorios selváticos.</br></br>

Es necesario que el campesino se “afinque” en su tierra, eche raíces en su territorio, desarrollando mejoras – casa, obras básicas, cultivos permanentes- y construyendo tejido social y comunitario. Para hacerlo, es indispensable la titularidad de la tierra. Nadie va a “invertir” en tierra que no le pertenece o de la que lo puedan expulsar.</br></br>

La presencia de los cultivos de coca está también asociada al tema de la tierra. El entierro de quinta que de una tímida reforma agraria hicieron, el 9 de enero de 1972 en el municipio de Chicoral, Tolima, los  políticos representantes de los partidos tradicionales del país, terratenientes y grandes hacendados de Colombia con el “Pacto de Chicoral” dio lugar a una mayor concentración de la propiedad y propició, además de violencia y persecución a los líderes agraristas, colonizaciones y desplazamientos a territorios marginales donde los cultivos ilícitos, promovidos por redes del narcotráfico, se erigieron en la única o mejor opción.  </br></br>

Por ello, con la misma lógica, originada en la ausencia de titularidad de los predios y la tierra, es que la erradicación forzosa, junto con la confrontación armada y el miedo, propician el desplazamiento de las familias campesinas sin tierra a colonizar “nuevos” territorios y la resiembra de cultivos ilícitos en tierras que no les pertenecen y de las que saben, tarde que temprano, serán expulsados. Los informes del Gobierno y sus expertos, tan dados a las piruetas semánticas, llaman este fenómeno como el “desplazamiento” de los cultivos ilícitos.  Una precisión: quienes se desplazan y caminan sin un rumbo cierto son campesinos sin tierra, “sin finca” y, con ellos, las matas de coca.</br></br>

La falta de titularidad y de claridad de las familias campesinas sobre su relación con los predios y la tierra, en vastas y aisladas regiones del país, es una condición útil y necesaria a la prevalencia de economías ilegales como la coca.</br></br>

Los datos no mienten. En el informe de OXFAM, de mayo de 2017, Radiografía de la Desigualdad Lo que nos dice el último censo agropecuario sobre la distribución de la tierra en Colombia, se comprueba cómo en los países andinos que más coca producen es donde las concentraciones de tierra y de las explotaciones agropecuarias son la más altas de la región: Colombia (81%), Perú (77%) Bolivia (66%), a diferencia de Ecuador (23%), país que después de Uruguay (17%), tiene la más baja concentración en tierra de las explotaciones agropecuarias y en el que, a pesar de presentar condiciones similares a Colombia, Perú y Bolivia, no han prosperado los cultivos ilícitos.  </br></br>

Ecuador no tuvo “Pacto de Chicoral”; tuvo a Eloy Alfaro y la Revolución Liberal ecuatoriana cuya reforma agraria fue una de las primeras de América Latina con la Ley de Beneficencia de 1908. Colombia no tuvo un Eloy Alfaro, pero tiene un Acuerdo de Paz y su reforma rural integral por implementar.</br></br>


