		<p> <strong>Por:  Gustavo Bolívar Moreno </strong> </br>

			Era miércoles 12 de mayo, jornada número quince del Paro nacional, algo impensable para mí, y creo que para toda Colombia, incluido el gobierno acostumbrado a que los paros se terminan por cansancio más no por diálogo. Uribe sabe que los paros duran cuatro horas luego de las cuales, todo el mundo se marcha a sus casas con las manos vacías.
El día citado, unas tres mil personas, en su mayoría jóvenes, marchábamos en completa paz, y también en completa amargura, por la Autopista Norte de Bogotá. Veníamos caminando desde la calle 81 donde queda el Monumento a los Héroes. Una mole enchapada en piedra muñeca, donde se rinde homenaje a los soldados de los diferente ejércitos libertadores y a una estatua de Simón Bolívar sobre su caballo, tallada en bronce.</br></br>

En ese lugar se quedaron otros tres o cuatro mil jóvenes danzando, cantando, puteando a Duque y a Uribe en medio de un plantón que ya se volvió habitual en ese lugar que anoche nos brindó un verdadero espectáculo y unas fotografías memorables como testimonio de una de las auto convocatorias más grandes en la historia de la ciudad. </br></br>

A la altura de la calle 167, a lo lejos, bajo el puente vehicular de la calle 170 se divisó una fila de hombres vestidos de negro, en perfecta formación. Eran los hombres del ESMAD. Enseguida quienes iban en la “Primera Línea” levantaron un puño de la mano izquierda y la seña se repitió con efecto dominó hasta quienes íbamos en el centro de la marcha y luego hasta los que la cerraban. Uno de los chicos, de no más de 22 años, portando un escudo de lata en uno de sus brazos tomó un megáfono, muy pequeño para la multitud que lo iba a escuchar y se dirigió a nosotros. Con quienes iba en la caminata nos tuvimos que acercar para escuchar lo que decía:</br></br>

“Compañeras, compañeros, es posible que nos empiecen a gasear o a disparar los del ESMAD. No sientan miedo que la Primera Línea está para cuidarlos. Los vamos a cuidar. Si nos mantenemos unidos no nos va a pasar nada. Pero si alguno de nosotros cae, si nos sucede algo, si nos hieren, quiero pedirles un favor. No nos dejen tirados. Recójannos y llévenos a una clínica, no nos dejen botados porque ellos llegan a rematarnos o nos llevan heridos a una Uri y allí nos dejan morir”</br></br>

Fue inevitable para muchos conmovernos hasta el último rincón del alma y para algunos contener al menos una lágrima. Esa valentía, esa entereza, nos pasmó el miedo a todos. Muy pocos desertaron luego de su advertencia aunque no niego que la adrenalina se atasca a la altura de la garganta. Los pensamientos malos se hacen presentes. La película de la vida empieza a pasar veloz por las cabezas de los más fatalistas.</br></br>

Los chicos que acompañaban a quien habló, de las mismas edades, se acomodaron el casco, agarraron bien fuerte sus escudos, casi todos llenos de impactos de balas de fogueo o de perdigones, incluso de “plomo es lo que hay plomo es lo que viene”. Otros escudos tenían el tatuaje que dejan esos artefactos metálicos donde vienen los gases o balines y puntillas que yacen en las bolsas recalzadas como la que mataron a Dylan Cruz. Son las huellas de la muerte. Las huellas del desprecio más inhumano de un gobernante por su juventud. Las huellas de la malparidez. La huella del odio, la huella de la oporofobia que practican las élites que prefieren comprar tanquetas, escopetas y balas asesinas, antes que alimentar al pueblo hambriento. La misma filosofía que practican las EPS: Prefieren gastar los millones en ejércitos de abogados contestando tutelas que en médicos para sanar y salvar a la gente.</br></br>

Mientras avanzábamos hacia el choque, estábamos a dos cuadras, quise intentar alguna fórmula desesperada para evitar lo inevitable, me acerqué al chico que habló y le pregunté si estaban seguros de lo que iban a hacer y hasta traté de convencerlos de terminar allí la marcha. No por cobardía, lo juro. Solo que vi en sus ojos a los de mi hijo que puede tener la misma edad y pensé que si ese chico fuera él lo sacaría de allí a rastras si fuera necesario para cuidar su integridad y ya vimos hasta la saciedad, que hasta su vida. Me dijo “no senador, ellos nos han robado hasta los sueños. Lo único que nos quedan son las calles. Si se las dejamos lo perderemos todo. Usted no se preocupe que nosotros lo vamos a cuidar”. Me dejó sin argumentos porque se supone que soy yo como servidor público el que los tengo que cuidar a ellos pero enseguida supe que mi concepto de valentía cambiaba desde ese día. Porque es muy valiente decirle cosas a Uribe y a Duque por tuiter y muy arriesgado enfrentar virtualmente a los grupos armados o a algún tuitero agresivo, pero otra cosa es saber que te puedes morir, que te pueden sacar un ojo o te pueden llevar preso dentro de un par de minutos y no huir, no hacer algo por evitarlo porque los ideales están tan altos que ni el miedo puede alcanzarlos.</br></br>

A una cuadra del enfrentamiento pensé: Ojalá no les suceda nada, Señor. Sus miradas no se me olvidarán nunca. Imagino que son las mismas que pusieron los 300 cojonudos del ejército de Leonidas, rey de Esparta, antes de enfrentar a mil veces más persas en la batalla de las Termópilas mientras que la mía, mi mirada, no era otra que la de alguno de los 12 apóstoles (No los de Yarumal, Antioquia) cuando vieron a los romanos matando de a poquitos a Jesús: Pura impotencia. Impotencia por no tener el don de la palabra para convencerlos de seguir la vida sin necesidad de arriesgarla por un país que luego los olvidará. Impotencia de no tener el poder para ordenarles a los del ESMAD que estos chicos podrían ser sus hermanos o sus hijos y que están hechos de lo mismo que están hechos los colombianos de los sectores más pobres a los que pertenecen ellos: de ilusiones, aguante, sueños, frustraciones.

			</br></br>
Milagrosamente al llegar a la calle 170 los hombres del ESMAD, igual de asustados a los de la Primera Línea se apostaron a lado y lado de las columnas del puente y la marcha pudo pasar. Nosotros nos fuimos al rato y sé que quince cuadras más adelante la marcha fue disuelta con gases. Afortunadamente, aunque estuve pendiente de las noticias, ninguno de los chicos fue herido y pudieron regresar sanos y salvos con sus familias. No quiero imaginar la angustia de las madres cuando ellos salen de sus casas decididos a cambiar un país con su escudo hecho con canecas partidas por la mitad. No quiero imaginar la ilusión de los chicos por mejorar el país donde en suerte les tocó vivir, que digo, sobrevivir. </br></br>

		Al día siguiente, lo vimos en los videos, uno de ellos, Sebastián Quintero cayó en Popayán. Un artefacto disparado desde un lanzamisiles, pomposamente llamado Vemon, le destrozó el cuello y la garganta. Es tan peligroso ese engendro que en un video donde se entrena a quienes lo van a disparar, el instructor  les dice que disparen “más alto” que disparen más alto. Y esto a pesar que viene instalado en la parte alta de la tanqueta. Es decir, el protocolo dice que hay que dispararlo al cielo para que caiga como la lluvia. Sin embargo, como lo vimos en otro video, uno de los 560 que entregaré mañana a la CIDH y al representante Mc Govern, presidente de la Comisión de derechos Humanos del Congreso de los Estados Unidos, los monstruos del ESMAD, porque no se les puede decir otra cosa, quizás por órdenes de algún oficial sanguinario, bajaron el Venom del techo de las tanquetas, lo pusieron a ras de piso y lo dispararon a la multitud de forma horizontal. Es decir, disparan a la humanidad, al cuerpo, a la cabeza, a los ojos de los muchachos de la Primera línea. </br></br>
