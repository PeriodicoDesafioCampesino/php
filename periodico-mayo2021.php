<font size="4">
	  <p> 
	   El director para las Américas de Human Rights Watch es un especialista en la región y está ahora investigando los casos de violencia reportados en las últimas semanas. Pide al Gobierno que deje hacer su trabajo a las organizaciones internacionales y cree que la polarización del debate público "minimiza los abusos de un lado y exagera o pone el acento en las violaciones de otros" 
	  </br></br>

 <strong> ¿Qué es lo que más le preocupa del desarrollo de las manifestaciones de los últimos días en Colombia? </strong> </br></br>

Lo más grave son los abusos de la policía, la tensión en las calles y la violencia por parte de los manifestantes. Durante los últimos nueve días hemos recibido un creciente número de denuncias, muchas de ellas creíbles, otras que tenemos en proceso de verificación, sobre asesinatos de civiles, detenciones arbitrarias, heridas graves, e inclusive casos de manifestantes que han sido atropellados con vehículos de la fuerza pública. </br></br>

Esto, en un contexto de creciente violencia. Si bien es cierto que las protestas arrancaron mayoritariamente de forma pacífica, la presencia de jóvenes que cometen delitos de vandalismo y de saqueos ha ido en aumento. De acuerdo al registro que nosotros tenemos hay más de 100 buses del transporte público que han sido destrozados. Igual ha sucedido con un sinnúmero de estaciones de policía. Y lo inquietante es que esta situación no mejora, se mantiene.  </br></br>

Las últimas cifras que nosotros pudimos confirmar nos daban un grupo de 36 casos de personas que han muerto en contexto de protestas, de los que hemos logrado identificar a 11. Es decir, que de esos 11 tenemos mayores antecedentes en cuanto al nombre completo de la víctima, datos y las circunstancias de la muerte. En estos casos creemos que se tratan, según toda la evidencia que tenemos, de víctimas de la fuerza pública. Los otros 25 casos, hasta llegar a los 36, son casos que tenemos en proceso de verificación. </br></br>

Lo más notable de todo esto ha sido el grado de brutalidad policial con el que ha actuado tanto el ESMAD (Escuadrones Móviles Antidisturbios), como la policía regular. Pero también los niveles de violencia al que han llegado algunos manifestantes que han logrado realmente hacer un gran daño a la propiedad, a la infraestructura, al bloqueo de caminos, y, en fin, que están dificultando incluso el transporte de alimentación y medicamentos en un contexto de pandemia. </br></br>

Se habla de 379 personas desaparecidas hasta el momento. </br></br>

Sí, hemos estamos documentando esos casos también. No obstante, nosotros no estamos, por lo pronto, en condiciones de confirmar la existencia de fenómenos de desaparición de personas. Hay que tener mucho cuidado con el concepto de desaparición forzada porque supone la detención arbitraria de civiles, un hecho que constituye un crimen de lesa humanidad. Hasta ahora, lo único que sabemos es que la Defensoría del Pueblo habló de alrededor de 150 casos de personas desaparecidas, y la policía dijo que de esos 150 había dado con el paradero de unos 50. Esa es la última noticia que tenemos. Pero nosotros no estamos en condiciones de afirmar que se estén dando desapariciones. Sí estamos en condiciones de afirmar que hay casos, y muy numerosos, de brutalidad policial. </br></br>


<strong>El problema del exceso en el uso de la fuerza por parte de la policía en Colombia viene de años atrás. ¿Cuál es el análisis de Human Rights Watch al respecto? 
</strong> </br></br>

Parte del problema es que la policía colombiana hoy es una institución, digamos, fuertemente militarizada. Esto se debe a que desde mediados del siglo pasado ha estado bajo el control del Ministerio de Defensa. Es el único cuerpo policial en América Latina con esa particularidad. Yo creo que ya es hora de que Colombia traslade a la policía a la supervisión del Ministerio del Interior o del Ministerio de Justicia. </br></br>


Ahora bien, la policía colombiana fue una de las pocas instituciones que a lo largo del tiempo fue recobrando credibilidad y prestigio. Sufrió grandes reformas en la década de los 90, cuando la dirigía el general Rosso José Serrano. Hubo reformas intensas y hubo una fuertes depuraciones donde miles de policías fueron destituidos y procesados por corrupción, por narcotráfico, por violaciones a los derechos humanos. La policía recobró unos niveles de profesionalismo que no tenía el resto de la fuerza pública, mucho menos el Ejército. 
</br></br>

Sin embargo, en el último tiempo creo que se han ido relajando los controles. Ha faltado disciplina, liderazgo, revisión de la conducta. Y, desde luego, eso se vio en las manifestaciones de noviembre del 2019 y con posterioridad, en septiembre del año pasado, después del asesinato del abogado Ordóñez, donde resultaron muertas 13 personas en manifestaciones. 
</br></br>

<strong>La creciente polarización en Colombia ha arrastrado el debate público a un escenario binario de los hechos ¿Cómo recobrar algo de claridad desde un enfoque de los Derechos Humanos? </strong> </br></br>

Esto mismo ocurre, lamentablemente, en los Estados Unidos, en España, en Argentina, en Brasil, o en México. Estamos pasando por unos procesos de creciente polarización donde la gente tiende a actuar con miopía con respecto a fenómenos que requerirían en principio la misma condena. Hay doble rasero porque, según mi ideología, minimizo los abusos de un lado y exagero o pongo el acento en las violaciones de otros. </br></br>

Justamente teniendo en cuenta esa realidad, creo que le corresponde especialmente al Gobierno de Colombia dar el ejemplo. Es decir, romper con esa visión parcializada, sesgada de la realidad y afrontar los hechos en términos categóricos de rechazar con la misma determinación todos los hechos violentos. El vandalismo, los saqueos, por supuesto. Pero con la misma energía debe condenar los abusos policiales. </br></br>

Yo no he visto, lamentablemente, y esto es parte del problema, una actitud transparente de este tipo por parte del presidente Duque. Yo creo que al presidente le ha faltado claridad, le ha faltado liderazgo. Ha actuado con un grado de ambigüedad frente a todo este fenómeno que no ayuda. Él ha estado muy en la línea de condenar la violencia y el vandalismo, cosa que obviamente es plenamente justificada, y hay que hacerlo y con mucha claridad, pero no lo he visto con el mismo ímpetu, advirtiendo a los policías que deben cumplir profesionalmente con su conducta, con un desempeño profesional y riguroso, sin que eso se traduzca en exceso en el uso de la fuerza y en hechos de brutalidad.  </br></br>

Eso haría falta. Pero no solo el presidente, sino también el jefe de la policía, el Ministro de la Defensa y otras autoridades. Si eso no ocurre, la sensación que queda es que el Gobierno avala la mano dura. Y por mano dura se entiende la brutalidad y eso indudablemente no ayuda ni a desactivar, ni a bajar las tensiones. Así no se va a generar un clima de negociación, ni un ambiente de respeto a los derechos fundamentales. </br></br>

<strong>En la misma línea: ¿Cómo evalúa el hecho de que el presidente Duque haya ordenado "asistencia militar" para controlar los disturbios en las ciudades más afectadas por los desmanes? </strong> </br></br>

Yo diría que ha sido uno de los más serios errores del presidente Duque. Acudir al ejército, a soldados armados preparados para el conflicto como una manera de fortalecer la presencia policial, es una medida equivocada. Las policías colombianas deben estar en condiciones y deben estar preparadas para restablecer el orden público, proteger a las personas, a la población y los bienes sin tener que recurrir a soldados. </br></br>

Soldados que están entrenados y preparados para la guerra, para el conflicto armado. Más aún, y especialmente los soldados colombianos, mucho más que cualquier otro soldado latinoamericano, porque son soldados que han estado, que han vivido y respirado y han nacido en un ambiente de 50 años de conflicto. </br></br>

Los combatientes entienden que pueden usar la fuerza letal cuando identifican un blanco militar legítimo. Y todo esto se agrava mucho más cuando uno escucha a autoridades de Gobierno, no sólo del Ejecutivo, sino también de la Fiscalía del doctor Barbosa, sosteniendo de que hay infiltrados en los manifestantes, que hay delincuentes del narcotráfico, que hay guerrilleros del ELN y de las disidencias de las FARC. </br></br>

Eso es supremamente peligroso porque contribuye a estigmatizar a aquellos que están ejerciendo su derecho a protestar pacíficamente. Y por supuesto también contribuye a incrementar los riesgos de aquellos que se pronuncian violentamente en la protesta. Aquellos que cometen vandalismo cometen un delito y deben ser enjuiciados por ello. Pero no son guerrilleros, narcotraficantes, ni mucho menos terroristas. </br></br>

Ojo con ese tipo de lenguaje, porque lo que pasa por la cabeza de un soldado cuando él entiende que está enfrentando a guerrilleros y terroristas, es que pueden perfectamente sentirse autorizados para cometer aún más graves abusos. Si existen infiltrados en las manifestaciones, si existen narcotraficantes, crimen organizado, si efectivamente hay elementos de las disidencias de las FARC o del ELN, eso hay que acompañarlo con pruebas, pruebas sensatas, pruebas sólidas, no simplemente acusaciones alegres, sin fundamento. </br></br>

Eso es echarle gasolina al conflicto. Es un grave error. Insisto, salvo que el Gobierno tenga esos antecedentes y efectivamente pueda demostrarle al país, a la opinión, a la comunidad internacional, que esto está ocurriendo en las calles, es una gran irresponsabilidad y una conducta que no contribuye para nada.  </br></br>
<strong>
La canciller colombiana –que dimitió el domingo después de haber sido realizada esta entrevista- criticó la semana pasada a la comunidad internacional, incluida la ONU, por la condena del uso excesivo de la fuerza por parte de la policía colombiana contra los manifestantes. ¿Resulta fácil el trabajo de HRW en Colombia?  </br></br>
</strong>
Es lamentable la actitud de aquellos que dirigen la política exterior de Colombia. Resulta paradójico que se sientan incómodos con la labor de observación internacional, cuando en realidad organizaciones como las Naciones Unidas y otras de carácter humanitario como la nuestra, creo que ayudan a oxigenar la coyuntura. Nosotros podemos aportar información documentada, sin sesgos, simplemente con el propósito de  mostrar un récord objetivo de lo que está ocurriendo y que permite indudablemente a la autoridad hacer las correcciones del caso. Creo que el Gobierno es el más interesado en que la información que podemos proporcionar le ayude a comprender con la mayor precisión lo que está ocurriendo en la práctica y sobre esas bases tomar las decisiones que se requieren. </br></br>



