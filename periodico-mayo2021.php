	  <p> 
	  	Estas poblaciones y sus economías se han articulado a los centros urbanos para abastecer de alimentos a los diferentes sectores sociales asentados en las ciudades en medio de muchos obstáculos y pérdidas, que han vulnerado las posibilidades de producción y abastecimiento que permitirían mayor acceso a los alimentos por parte de los colombianos. </br></br>

El proceso para abastecer las grandes ciudades y garantizar la realización del derecho a la alimentación de la población colombiana rural y urbana, se vuelve un reto constante que cuenta con muchas dificultades, para las cuales debería existir una política humanitaria y no solamente comercial y económica que permita regular: el control corporativo de las semillas, el alto precio de los insumos, el bajo costo de compra a los productores, el incremento de las importaciones de alimentos, que en su conjunto terminan sacando a pequeños productores del mercado y reduciendo sus ingresos, sometiéndolos a sistemáticas pérdidas, que los han llevado a dejar sus tierras para migrar a las ciudades a buscar trabajos informales y elevar la supuesta competitividad de las ciudades a partir de salarios precarios que generan más situaciones de hambre. </br></br>

Cuando esto ha ocurrido sistemáticamente durante las últimas décadas y las ganancias han fluido por las carreteras continuamente y sin contratiempos, el silencio hace cómplices a muchos sectores. Nadie ubica las relaciones del abastecimiento con: los precios de la gasolina, el precio de lo peajes, el mal estado de las vías, el alto precio de los insumos, la privatización de las semillas, la concentración de la tierra. Tampoco se relaciona la disponibilidad de alimentos con las condiciones de trabajo de los productores y sus bajos márgenes de ganancia, no se asocia esto con las políticas de libre mercado que terminan deteriorando las condiciones de vida en la ruralidad y cómo esto fractura y restringe las posibilidades de autoabastecimiento al deteriorar los mercados locales y el tejido social rural. Estos procesos han afectado el abastecimiento alimentario, al concentrar las ganancias en importadores, grandes productores y en grandes supermercados, mientras se cierran plazas municipales y tiendas de barrio en todas las ciudades y regiones del país. </br></br>

Solo se habla del desabastecimiento cuando los productos perecederos duran días en los camiones y se pierden en carretera, cuando los transportistas o los comerciantes se ven afectados por las protestas y los bloqueos. Ahí es cuando toma importancia el abasto alimentario como preocupación, generando alarma acompañado del miedo y sensación de inseguridad. Nada coherente y más bien oportunista preocuparse ahora por el desabastecimiento y el hambre cuando el conjunto de políticas económicas que originan el paro y los bloqueos se hacen frente a las políticas que han restringido la posibilidad de ser abastecidos por pequeños y medianos productores colombianos. Cuando hay bloqueos generados en las protestas el abasto alimentario es un derecho restringido por las protestas, pero cuando las ganancias fluyen el silencio es la constante. Así que aquí el problema central no es el hambre ni el desabastecimiento de la población en general, el problema son las ganancias de unos sectores. </br></br>

Es importante que el paro y la protesta sigan dando señales y enseñanzas éticas y humanas como las que se vienen conociendo en diferentes regiones a partir del establecimiento de corredores humanitarios.  En más de 60 sitios del país, como lo señala la Defensoría del Pueblo. Esto ha hecho posible el abastecimiento de los productos esenciales a partir de acciones humanitarias, que no son implementadas cuando los alimentos y la salud son tratados como cualquier mercancía en el mercado y no como bienes que garantizan un derecho humano. Este es un momento lleno de posibilidades para la construcción de un circuito agroalimentario humanitario a partir de una convocatoria clara por el derecho a la alimentación solicitada al gobierno, a los gremios y a los comerciantes, que permita garantizar el “pan nuestro de cada día” a las familias colombianas, como un paso fundamental en la construcción de confianza y paz. </br></br>

No es coherente alarmar y generar temor en la población con un posible desabastecimiento para señalar específicamente al paro como responsable de generar y someter al hambre a la población de las ciudades. El paro no es el generador del hambre de la población, las banderas rojas señal de desabastecimiento de las unidades domésticas, no aparecieron con las protestas del paro nacional, tampoco la situación de pobreza y desempleo, menos la imposibilidad estructural del acceso a alimentos. Estas situaciones son producto de las políticas económicas y sociales de las últimas décadas. La protesta social es una respuesta a todas las medidas que han generado el hambre y las restricciones al acceso de alimentos. La protesta tampoco es la responsable del abandono del campo colombiano. Usar la alarma alimentaria como deslegitimadora del paro es una estrategia irresponsable de los promotores de las políticas que han llevado a miles de colombianos a no contar con “el pan nuestro de cada día”. Con hambre es muy difícil construir confianza y llegar a acuerdos. Con hambre no hay paz y con su presencia silenciosa la protesta es y será legítima. </br></br>


