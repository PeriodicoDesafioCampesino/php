		<p>
			Con hambre es muy difícil construir confianza y llegar a acuerdos. Con hambre no hay paz y con su presencia silenciosa la protesta es y será legítima.
Causa escalofrío el solo hecho de imaginar la situación y los sentimientos de las familias que en el último año han encontrado desabastecidas sus cocinas, alacenas y neveras. El no poder llevar los alimentos a sus casas les ha llevado a comer solo una o dos veces al día; esta situación es la más lejana a la tranquilidad y la paz de las personas que integran estos hogares. Esta es una situación silenciosa que han vivido miles de familias en Colombia, que debería ser difundida con la misma dureza con la que aparecen las alarmas que comunican un posible desabastecimiento alimentario producido por el paro y las diversas acciones de protesta.  Antes de hacer estos juicios sobre la protesta como generadora de hambre, es pertinente pensar y reconocer las condiciones en las que ha operado el abastecimiento alimentario en el país durante las últimas décadas.</br></br>

Silenciosamente, día a día millones de productores cultivan alimentos, mientras los acopiadores y transportistas pasan días y noches por ríos, trochas, caminos y carreteras moviendo los alimentos para llegar a descargarlos en el amanecer en medio de intercambios acelerados que acontecen en distintos mercados minoristas, mayoristas, plazas de mercado regionales y municipales. Unas horas antes del amanecer los camiones llegan desde distintos sitios y veredas del país con gran variedad de productos. En medio de la noche los coteros corren haciendo equilibrio en delgadas tablas para la descarga de los camiones; antes del amanecer, en los barrios y cabeceras municipales los tenderos se levantan para dirigirse a estos mercados en búsqueda de los mejores precios de alimentos en cosecha, que facilitan el abastecimiento de familias en algunos casos al fiado considerando las restricciones económicas actuales. Así, según cifras del Dane, cotidianamente y en el tiempo transcurrido entre los años 2012 y 2017 desde las veredas llegaron a las centrales mayoristas de las diez principales ciudades de Colombia, cerca de 25 millones de toneladas de alimentos: 26% hortalizas, 27% tubérculos, raíces y plátanos, 26% frutas y de otros grupos 21%. Solo para abastecer a una persona promedio de Bogotá se estima que se requieren 0.7 hectáreas de tierra y para producir todos los alimentos que llegan a Bogotá en un año, se necesitan para regar los cultivos el agua que reúnen el equivalente a 11 lagos de Tota.</br></br>

De la apropiación y el uso silencioso y cotidiano de este conjunto de bienes ambientales y procesos de trabajos que sostienen el abastecimiento alimentario de los colombianos, se hablaba muy poco, solo hasta que la protesta social y los bloqueos aparecen, restringiendo la circulación de mercancías y dinero, generando pérdidas económicas para algunos sectores. Así, por fin, aparece en escena con dureza, el miedo que acompaña las alarmas y la realidad del “desabastecimiento” generado específicamente por los bloqueos que restringen la circulación de las ganancias. Esta situación contrasta con el silencio publico cuando las familias con restricciones para el acceso a los alimentos se encuentran desabastecidas, allí, tímidamente y con vergüenza, las banderas rojas aparecen en las ventanas de las casas y apartamentos comunicando la situación de hambre, como un dolor silencioso que carcome los estómagos, situación que no genera alertas, ni alarmas en los medios. Cuando las ganancias fluyen, el abasto alimentario es invisible nadie habla de él y la sociedad sistemáticamente olvida el recorrido de los alimentos y la vida de productores y transportistas que mueven los alimentos para que lleguen a la mesa de los comensales, de aquellos que hoy pueden comer sus tres “golpes” en medio de los duros y crudos días de nuestro país.</br></br>

El abasto alimentario y sus problemas no corresponde únicamente a la coyuntura actual, no son el paro y los bloqueos los generadores de los problemas históricos del país en este tema. Hace cuatro décadas las cifras del abasto alimentario de Colombia mostraban que los colombianos contábamos con una producción que permitía el autoabastecimiento alimentario superior al 70 o 80 % en muchos productos que en la actualidad son importados. Esto permitía fortalecer los mercados alimentarios locales y regionales que convergían a nivel nacional. Buena parte de estos alimentos provenían de la economía campesina y de pequeños y medianos productores, cultivados en suelos de ladera de aquellas tierras donde los pueblos rurales lograron arraigarse, en medio de luchas por la tierra desde inicios y mediados del siglo XX, para sostener sus vidas y sembrar comida para ellos y para el país.</br></br>




