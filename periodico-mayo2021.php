</br></br>
El paro sigue porque las causas se acumularon con las décadas. Hay 80 mil desaparecidos durante el conflicto. Hay casi 300 mil muertos que claman justicia entre ellos los 6.402. Hay una desigualdad monstruosa, inhumana, ilógica que no repara en consecuencias. Hay desempleo, hay hambre, hay privilegios a los que solo los políticos, sus familias y sus familiares pueden acceder. Hay una corrupción aberrante. Se roban todo y después vuelven por las sobras.  Hay un país aparente que durante las últimas dos décadas se ha jactado de crecer a mayor ritmo que el promedio latinoamericano pero no dicen las estadísticas que esa riqueza y ese crecimiento se ha quedado en muy pocas manos. Hay una tierra pésimamente repartida. Hay unas etnias arrinconadas en tierras improductivas. Hay abuso de poder. Hay impunidad para los ladrones del erario. Hay estudiantes endeudados hasta la madre por querer estudiar en vez de irse a una bacrin. Hay racismo terrible. Los supremacistas mestizos odian a los indígenas a quienes llaman “indios” y los mandan a matar, los mandan a encerrarse, los mandan al cementerio desde todos los flancos: El narco, las disidencias, el gobierno y ahora los civiles vestidos de blanco, “la gente de bien de Cali” angustiada porque el paro les está dañando el negocio de la cocaína.</br></br>

Por eso el Paro no tiene Jefes. Cada quien lucha por algo distinto. Los camioneros, por ejemplo, luchan contra la cantidad de peajes y sus costos, el alto precio de los combustibles y la pésima red de carreteras secundarias y terciarias. Los jóvenes luchan porque la clase política los marginó de la redistribución del ingreso. No tienen educación, no tienen empleo y sus posibilidades de pensionarse son casi nulas. Los maestros paran por la estigmatización que de su profesión ha hecho el uribismo. Los indígenas paran porque Duque les incumplió los Acuerdos, no implementa la paz que los tiene en la mira de los grupos violentos. cada quien lucha por lo suyo pero entendieron que juntos son más poderosos.  </br></br>

Por eso el paro no tiene un líder. Cada quien responde por su causa y no están dispuestos a venderla barata. Los y las jóvenes llegan solos, por inercia, por necesidad, pero por sobre todo, porque ya saben que son importantes, que son capaces de tumbar ministros y hacer retirar proyectos nefastos en el Congreso como la Reforma Tributaria y la Reforma de la Salud que ya está muerta.</br></br>

Pero hay algo más preocupante aún. Los jóvenes de Puerto Resistencia, por mencionar un solo sitio, no darán el brazo a torcer. Están dispuestos a morir por la causa. Saben que no tienen nada qué perder. Es tan cruel lo que escuché que lo tienen que saber. Muchos han dicho que no levantan el paro porque nunca antes habían comido tan bien. Esto es porque las madres, sus madres, apoyando las causas que ellas no fueron capaces de defender cuando jóvenes, están apoyando a sus hijos. Les cocinan en grandes ollas y han recibido la solidaridad del barrio. En Bogotá las hemos visto, incluso, junto a los chicos de la primera línea diciéndoles a los del ESMAD que si son tan hombres les disparen. No lo han hecho ni lo harán. Hasta los irracionales se mueven bajo unos mínimos éticos.</br></br>

Y finalmente una aclaración. Al comienzo dije que marchábamos en completa paz y en completa amargura. Dije que caminamos en paz porque somos pacíficos, y amargados porque las cifras de jóvenes muertos en toda Colombia sigue creciendo con el paso de las horas. Ojalá no muera nadie más. Ni manifestante ni policía. Ojalá los chicos de la Primera línea terminen con sus ojos intactos este proceso, para que Colombia siga mirando a través de sus ojos ilusionados. Me declaro admirador de todos ellos y ellas. Los de Cali, los de Barranquilla, los de Medellín, los de Popayán, los de Pereira, los de Soacha, los de Buga, los de toda Colombia. Me arrodillo con respeto y reverencia ante su absurda valentía. Si de mí dependiera les haría un monumento. Juro que algún día lo tendrán. Les agradezco por cuidarnos. Son hermosos, inoculan valentía, irradian esperanza. Enseñan la poca distancia qué hay entre el miedo y la ternura. Dan ganas de abrazarlos, prepararlos y llenar el Congreso de ellos. Entrañable Primera línea: Los amamos.</br></br>

Espero que sus escudos no se pierdan. Esos escudos artesanales y pesados tienen que adornar el salón más importante de ese museo que tenemos que hacer para que Colombia no olvide la ignominia, no olvide a Uribe, no olvide a Duque a lo mejor sonriendo mientras firma el contrato de compra de las armas que acabarán con su pueblo. Un museo que le cuente a las futuras generaciones que ustedes existieron, el “Museo de la Infamia” donde las tanquetas del ESMAD y su mortífera arma Venom permanecerán eternamente inmóviles, observadas con horror por los visitantes.</br></br>

Vamos a acabar con esa infamia llamada ESMAD. A sus integrantes hay que acogerlos y mantenerlos en la Policía para que persigan delincuentes y narcotraficantes, incluidos los que nos gobiernan. Hay que entregarles educación, hay que devolverles el amor, sanarles el odio que le inocularon sus adoctrinadores. Los acogeremos con cariño.

